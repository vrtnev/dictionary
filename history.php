                    помета (или отсутствие ее) + толкование + две, три цитаты в качестве иллюстраций, -
                    критиковать легко. И полезно. Особенно для авторов-составителей. Многочисленные
                    замечания в рецензиях, касающиеся этих четырех компонентов словаря, - бесценный
                    подарок для авторов. Известно, что слово - это целый мир. Именно с многозначных
                    слов начинал обучение редакторов С.Г.Бархударов. По мнению Л.В.Щербы, "каждое
                    мало-мальски сложное слово в сущности должно быть предметом научной монографии"
                    (Опыт общей теории лексикографии... С. 285). В СлРЯ XI-XVII вв. таких слов немало, но
                    монографий - почти нет. И нередко, автору словарной статьи, для того, чтобы дать
                    толкование, выраженное двумя-тремя словами или занимающее одну-две строки,
                    приходится проводить целое научное разыскание, которое, конечно же, остается за
                    рамками словаря. "Понятно, - пишет Г.А.Богатова, - что за словарной статьей стоит
                    большая лексикологическая работа. И все же такой показ семантической истории
                    слова лексикографическими средствами, предусмотренными правилами СлРЯ XI-XVII
                    вв., мы считаем контурным, неполным" (Г.А.Богатова. История слова как объект
                    исторической лексикографии... С.107). Если прибавить к этому огромные объемы и
                    дефицит времени у авторов и немногочисленных редакторов, то отмеченные
                    читателями
                    недостатки и ошибки словаря могут быть легко объяснимы. Задачу же сохранения и
                    обнародования всей имеющейся в картотеке и в последних публикациях памятников
                    лексики "Словарь русского языка XI-XVII вв." решает. Следующие поколения
                    лингвистов внесут уточнения, дополнения и поправки во все части этого Словаря, в
                    котором будет собрана с наибольшей полнотой известная на сегодня лексика
                        указанного периода.</p>

                    <p>Зарубежные лингвисты, наряду с указаниями достоинств и недостатков СлРЯ XI-XVII
                    вв., говорящие о высокой квалификации рецензентов, нередко главный упрек
                    адресовали ученым, которые уже в названии словаря следуют "теории, принятой в
                    последние 50 лет: это возвращение к ассимиляционной политике империалистической
                    России, которая отторгает ранний период у украинского и белорусского языков, и
                    звучит почти столь же необычно, как скажем словарь французского языка I века"
                        (Н.G.Lunt.- Language. V. 55. N 4. 1979; и более ранняя - Vol. 52. N 3. 1976).</p>

                    <p>А.В.Исаченко писал о том, что русские не имели права присваивать только себе те
                        памятники, которые принадлежат трем народам: им, украинцам и белорусам. ???</p>

                    <p>На этот упрек еще в начале 20-х годов дали ответ академики А.Кримський и
                    А.А.Шахматов, опубликовавшие исследование и древние тексты в книге "Нариси з
                    iсторii
                    украiнськоi мови та Хрестоматiя з пам'ятникiв старо-украiнщини X-XVIII вв." (Киiв. 1924),
                    которая включила отрывки из Успенского сборника, из Изборника 1076 г. и др.,
                    являющиеся источниками и для СлРЯ XI-XVII вв. Уже тогда была очевидна их мысль о
                    том, что на материалах ранних памятников все восточнославянские народы могут
                        создавать историческую лексикологию и лексикографию своих языков.</p>

                    <p>Как писала Г.А.Богатова, "конференции по проблемам исторической лексикологии и
                    лексикографии, начавшиеся в 1961 г. Львовским совещанием по обсуждению Пробного
                    выпуска Староукраинского словаря, выявляли круг задач, стоящих перед словарями"
                    (История слова как объект исторической лексикографии... С.65). Уже состоялось
                    открытие века: в 1952 г. в Новгороде были найдены первые берестяные грамоты XI-XIV
                    вв. В 1977-1978 гг. вышел двухтомный "Словник староукраiнськоi мови XIV-XV ст." А
                    когда словарь был выдвинут на государственную премию, рецензен- тами выступили
                    сотрудники СлРЯ XI-XVII вв. В 1982 г. начал выходить многотомный "Гiстарычныi слоунiк
                        беларускай мовы" XV-XVIII вв. (в 19 ??? опубликован ??? том).</p>

                    <p>Рецензенты первых томов отмечали, что СлРЯ XI-XVII вв. не является исчерпывающим,
                    так как в него не включены слова "найденные в Срезневском и Фасмере", имеющиеся
                    в "Хождении за три моря" Афанасия Никитина и Переписке А.М.Курбского с Иваном
                    Грозным (E.L.Keenan. - Kritika. 1978. Vol. 14. N 1); что Словарь не дает толкований,
                    удовлетворяющих историков материальной культуры и историков языка, что словарь
                    не
                    содержит стилистических помет, что самая ранняя фиксация по сути не является
                    таковой и пр. Г.Г.Ланту первые выпуски СлРЯ показались такими ошибочными, что он
                    советовал библиотекарям ставить при этом издании сигнал, говорящий об опасности
                        для тех, кто будет им пользоваться (Н.G.Lunt. Language. 1979. Vol. 55. N 4).</p>

                    <p>В рецензии А.В.Исаченко также отмечались многочисленные ошибки первых двух
                    выпусков, которые были допущены в них несмотря на то, что "в редакционную
                    коллегию
                    вошло 19 специалистов (языковедов, литературоведов, историков)". Он выступал
                    против
                    нарушения алфавитного порядка, когда Ъ не принимается во внимание в конце слов, но
                    принимается в середине, когда Ь стоит на месте Е. Отмечал снижение филологической
                    подготовки специалистов, составляющих словари и словоуказатели. И в то же время,
                    подчеркивая важность выхода такого словаря, называл "странной" традицию, которая
                    "заставляет называть творческую лексикографическую работу "составлением"
                        словаря" (Russian Linguistics. N 3. 1976).</p>

                    <p>Е.Вайер уделил в своей рецензии особое внимание источникам, на которых
                    основывается СлРЯ XI-XVII вв., выразил свою неудовлетворенность тем, что словарь не
                    стал тезаурусом, и заключил, что авторы, "ради научной публикации пошли на
                    компромисс, который не удовлетворяет ни тех, кто нуждается в настольном словаре,
                    ни
                    тех, кто ожидал появления научного справочного издания. Причины этого нужно
                    искать
                    не в чисто научных соображениях, - пишет он далее, - но скорее в том, что после
                    длившейся десятилетия подготовки издатели стремились скорее показать результаты"
                        (Anzeiger fur slavische Philologie. Bd. X-XI. 1978-1979).</p>

                    <p>Высказывалось мнение, что представить всю лексику в одном словаре такого широко
                    хронологического охвата - дело нереальное, нужно составлять словари согласно иной
                    периодизации, в которой бы выделялся каждый значительный этап в развитии языка
                    русского народа. Э.Кленин в своем отзыве предполагала, что "стремление к
                    серьезному улучшению Словаря... должно было бы привести по сути к осуществлению
                    всего проекта заново и с новым названием" (Slavic and East European Journal. 1979. Vol.
                        23. N 4).</p>

                    <p>Список рецензий на первые 9 выпусков СлРЯ XI-XVII вв. помещены в сборнике статей
                        "Теория и практика русской исторической лексикографии" (М., 1984. С. 256).</p>

                    <p>Все эти упреки, резкие отзывы и порой недоброжелательный тон, сыграли свою роль и
                    привели к тому, что главный редактор С.Г.Бархударов, ссылаясь на свой возраст, не
                    раз
                    просил Дирекцию ИРЯ освободить его от руководства словарем. Не помогла и
                    вдумчивая и в общем-то одобрительная рецензия, которую поместил в журнале Slavia
                    (Roc. XLVI, sesit 2. 1977) И.Немец, сам работающий над Старочешским словарем,
                    который начал выходить в Праге в 1968 г. под редакцией Б.Гавранека. В другой
                    рецензии он сравнил вышедшие в нашей стране к этому времени СлРЯ XI-XVII вв.,
                    Словник украiнськоi мови XIV-XV ст. (Киев, 1977-1978) и Словарь русского языка XVIII в.
                    Для уточнения смыслового определения И.Немец предлагал использовать достижения
                    разработанной чешскими лингвистами теории исторической лексикологии. А в конце, не
                    взирая на ограниченность объема словарей, призывал к "сохранению в них
                        лингвистической взыскательности" (Slavia. Roc. XLVIII. sesit . 1979).</p>

                    <p>Шестой выпуск СлРЯ XI-XVII вв. (1979 г.) был последним, над которым работал
                        С.Г.Бархударов.</p>

                    <p>Затем, с 1980 г., обязанности главного редактора взял на себя директор ИРЯ член-
                    корреспондент АН СССР доктор филологических наук Ф.П.Филин, уже и так
                    обремененный изданием "Словаря русских народных говоров". Он успел прочитать и
                    сделать свои замечания в 7-10 выпусках СлРЯ XI-XVII вв. до своей кончины в мае 1982
                        г.</p>

                    <p>Под его руководством Словарь изменил свое направление в сторону более детальной
                    разработки семантики слова, расширения цитатного материала, углубления дефиниций
                    и строго соблюдения хронологических границ: 1700-й стал последним для
                    используемого в Словаре цитатного материала. Ф.П.Филин предложил переработать
                        Инструкцию в соответствии с практикой составления Словаря.</p>

                    <p>В середине 70-х гг. в Группу ДРС влились историк-источниковед канд. исторических
                    наук С.П.Мордовина и источниковед-филолог канд. филол. наук Л.Ю.Астахина,
                    ставшая
                    заведующей Картотекой ДРС. Под их руководством с 1975 г. проходила при Группе
                    ежегодная лексикографическая практика студентов в форме семинара, в ходе которой
                    составлялись словоуказатели к изданным сборникам памятников XVII в., пополнялась
                        картотека.</p>

                    <p>В 1981 г. по инициативе Ф.П.Филина Группа ДРС при дирекции ИРЯ была
                    преобразована в сектор исторической лексикологии и лексикографии (с 1984 г. -
                        Отдел). ???</p>

                    <p>По его же предложению, в 1980-1981 гг. накопившим опыт коллективом была
                    переработана Инструкция, опубликованная в 1988 г. тиражом в 200 экземпляров. В ней
                    подчеркивалась преемственность с инструкциями Б.А.Ларина (1936 г.), Б.А.Ларина и
                    С.П.Обнорского (1940 г.), С.Г. Бархударова, А.П.Евгеньевой, С.Ф.Геккер (1950 г.),
                    С.Г.Бархударова, Г.А.Богатовой, А.Н.Добромысловой, О.В.Малковой, Г.Я.Романовой,
                    Е.М.Сморгуновой, О.И.Смирновой, Г.П.Смолицкой, Э.Г.Шимчук (1965 г.), которые в
                        какой-то части не только повторяли, но и изменяли предшествующие.</p>

                    <p>В Инструкции сообщалось: "В 1970 г. обсуждение подготовленных к печати материалов
                    показало, что словарь перерос Малый древнерусский словарь не столько по составу
                    словника, сколько по самому типу и информативности словарной статьи. Решением
                    редколлегии было изменено его название" (с.3). Новый текст "Инструкции" отражал
                    теперь изменения в практике составления Словаря, происшедшие с момента начала его
                        издания" (с.4), принимал некоторые пожелания рецензентов.</p>

                    <p>Основные задачи и назначение Словаря оставались прежними: "служить справочным
                    пособием для чтения древнерусских текстов разных жанров... для широкого круга
                    специалистов" (с.7). Но если в МДРС авторы обещали включить "возможно большее
                    количество слов" (Богатова Г.А. О работе над Малым древнерусским словарем XI-XVII
                    вв. // Изв. ОЛЯ. 1966. Т.XXV. В. 6. С. 524), то здесь ставилась задача
                    "инвентаризировать ис- конно русскую лексику, начиная с древнерусского
                    (общевосточнославянского) периода, а также лексику, которой пользовалась в
                    обиходе
                    русская письменность" (с.7). Уточнялось, что Словарь не является тезаурусом, что при
                    формировании словника решается вопрос отбора слов с учетом характера памятника,
                    семантики и бытования слова в русском языке. Оговаривалась группа слов, не
                    включаемых в Словарь: "неправильно транслитерированные и неверно понятые
                    переводчиками или переписчиками иноязычные слова" (с.7), заимствования,
                    "оформленные не по правилам русского языка" и находящиеся "в цитатах, где
                    отсутствуют проясняющие контекст русские слова" (с.11). Говорилось об ограниченном
                    введении индивидуальных новообразований "из поздних памятников письменности"
                    (там же). Ономастический материал включается либо "для ранней датировки слова",
                    либо "как имеющий самостоятельное значение: фиксирующий словообразовательную
                        модель, переносное или обобщающее употребление собственного имени" (с.12).</p>

                    <p>Относительно семантической схемы статьи было решено: в ней "должны быть показаны
                    все зафиксированные в картотеке значения слова, их оттенки, образные и переносные
                    употребления" (с.49). "Не только слова и их значения, но и зарегистрированные в
                    Картотеке ДРС разного рода устойчивые сочетания слов" объявлялись объектом
                        описания СлРЯ XI-XVII вв. (с.52).</p>

                    <p>В силу особенностей русского языка, в котором слова "живут семьями", авторы искали
                    и нашли такой способ организации материала, который связывал бы историю слова с
                    корневой группой. Этому был посвящен доклад Г.А.Богатовой на VIII Международном
                    съезде славистов в 1978 г. в ???... "Семантика корневой группы и история слова в
                        славянской исторической лексикографии".</p>

                    <p>Представленные в то время в Картотеке ДРС типы источников распределялись в тексте
                    Словаря так: около 19 % цитат - из источников XI-XII вв., около 13 % - из источников
                    XIII-
                    XIV вв., около 67 % - XV-XVII вв. Если при таком соотношении избрать в качестве
                    основной заголовочной формы древнейшее написание, то корневая группа распадается
                    на несколько 1 фонетических слов 0, написание которых алфавитно удалено друг от
                    друга и соответствует разным периодам. В таком случае пришлось бы
                    реконструировать древнейшее написание у 67 % слов. Предвидя это, Л.В.Щерба еще в
                    своем "Опыте общей теории лексикографии" (1940 г.) ставил перед составителями
                    исторических словарей вопрос: "Будем ли мы создавать ис- торию фонетических слов и
                    их значений или историю слов-понятий, или наконец, свяжем все это в одно целое, как
                    теоретически казалось бы бо- лее правильным" (Л.В.Щерба. Языковая система и
                        речевая деятельность. М., 1954. С. 304).</p>

                    <p>Выбор заголовочной формы, ориентированной на конец охватываемого Словарем
                    русского языка XI-XVII вв. периода, с приведением в скобках древнейшего написания и
                    регулярных, расположенных в соответствии с хронологией вариантов, позволяет
                    связать в единое целое и обойтись - ради размещения корневой группы в одном
                    алфавитном отрезке - минимумом реконструкций заголовочной формы слова (Богатова
                    Г.А. От исторического словаря к исторической лексикологии русского языка. Опыт
                        автореферата. С. 15 рукописи ??? Где опубликовано ???</p>

                    <p>Об объемах и сроках окончания работы над СлРЯ XI-XVII вв. в Инструкции не
                    сообщалось. Это означало, что авторы должны были исходить из возможностей
                    Картотеки ДРС и новых изданий рукописей, в основном подготовленных Сектором
                    лингвистического источниковедения и исследования памятников языка под рук. проф.
                    С.И.Коткова. Если публикации содержали указатели слов, то слова из них шли
                    непосредственно в Словарь, минуя Картотеку, а процесс обновления цитатного
                    материала проводился непосредственно по изданию. В этом отношении Словарь богаче
                    картотеки. Но в него не попадали, по новой Инструкции, материалы Картотеки,
                        датированные XVIII веком, - это составляет преимущество КДРС.</p>

                    <p>После кончины Ф.П.Филина главным редактором выпусков 11-14 был утвержден
                    доктор филологических наук Д.Н.Шмелев (в 198 ???), избранный в это время членом-
                        корреспондентом АН СССР, а затем действительным членом.</p>

                    <p>Протокол обсуждения 11 выпуска ???</p>

                    <p>В последние годы материалы Картотеки ДРС стали все чаще привлекать внимание
                    исследователей, как лингвистов, филологов, историков, так и ученых, весьма далеких
                    от лингвистики, но имеющих отношение к русской и славянской истории. Видимо,
                    сбывается надежда академика М.Н.Сперанского, мечтавшего о такой картотеке,
                    которая была бы нужна не только лингвистам, но и всем, кто в своей работе касается
                        непосредственно прошлого России.</p>

                    <p>Пожар в Библиотеке Академии наук в 1988 ??? г. показал всю опасность мгновенной
                    утраты уникальных ценностей русского слова: Картотека ДРС хранится в деревянных
                    ящиках - дважды возникала такая опасность в ИРЯ. Пострадали карточки от воды,
                    хлынувшей с потолка прямо на стол, где стоял картотечный ящик. В 1993 г. такая
                        ситуация повторилась, но к счастью, карточки не пострадали.</p>

                    <p>Руководитель Отдела исторической лексикологии и лексикографии Г.А.Богатова,
                    обратившаяся во Всесоюзное агентство охраны авторских прав, узнала, что такие
                    собрания, как Картотека ДРС, не являются объектами, которые "нужно охранять".
                    Обращение в Институт государства и права АН, в Комиссию по культуре Верховного
                    Совета СССР привели к тому (или совпали?!), что государство начало разрабатывать
                    закон об авторском праве, о чем сообщил на заседании Ученого совета ИРЯ
                    руководитель этого проекта ??? Кузнецов в ??? году. На очереди закон об
                        интеллектуальной собственности.</p>

                    <p>Одними из первых заинтересовались вопросами копирования и распространения
                    материалов Картотеки ДРС американские ученые. Так, Х.А.Олмстед (Гарвардский ун-т)
                    привез в Отдел ксерокс Canon-12 и предложил наши нестандартные карточки
                    скопировать на обычный лист по 4 с каждой стороны, так, чтоб на листе уместилось 8
                    карточек. А для изготовления микрофиш отправлять эти листы в США. Был подписан
                    Протокол о намерениях (в 1989 г.) и таким образом был обработан материал на букву 1
                    Ш 0 в порядке эксперимента (более 10 тыс. карточек). Но микрофиш до сих пор нет, и
                    материалы на 1 Ш 0 не возвращены. Однако параллельно объединение "Электроника"
                    на аппаратах "Pentakta" на скромные деньги Института, выделенные директором
                    Ю.Н.Карауловым, изготовило микрофиши на букву 1 Т 0 (26 ящиков). Теперь ни
                        аппаратов для чтения, ни фирмы "Pentakta" уже нет.</p>

                    <p>Замечательный энтузиаст из "Автоэкспорта" А.А.Чувиков сделал на японских
                    аппаратах Seiko микрофиши карточек на букву 1 У 0, которые были предварительно
                        скопированы сотрудниками Отдела на ксероксе по 4 карточки на странице. Но ???</p>

                    <p>Отечественное отделение ЮНЕСКО добилось включения вопроса о сохранении
                    Картотеки ДРС в Программу "Память мира", которая ставит задачу спасения
                    уникальных объектов, составляющих национальную ценность. По этой программе в
                    1995 г. Отделение ЮНЕСКО выделило для Отдела 5 машин для перевода материалов
                    КДРС на лазерные диски с целью изготовления ее электронного дубля и
                    распространения его по крупным научным центрам мира: ведь поездка в Картотеку
                    ДРС становится в последнее время почти недостижимой роскошью для многих
                        исследователей.</p>

                    <p>С огромным напряжением сил Отдел начал выполнять эту работу, не прекращая
                    составления и издания Словаря. Это при том, что в Отделе осталось всего 10
                    сотрудников. Существенную помощь оказывает коллективу преподаватель
                    Московского государственного технического университета им. Н.Э.Баумана кандидат
                    технических наук Ю.Н.Филиппович, обучающий сотрудников работе на машинах и
                        изыскивающий возможности оказать помощь Картотеке ДРС.</p>

                    <p>В 1995 г. прошла Международная конференция, посвященная выходу 20-го тома
                    Словаря русского языка XI-XVI вв. и 70-летию Картотеки ДРС. В докладах,
                    прозвучавших
                    на этой конференции, было показано значение Картотеки ДРС для отечественной и
                    зарубежной науки. Были сказаны добрые слова в адрес Словаря, который несмотря на
                    недостатки, все же от- вечает основному своему назначению: инвентаризирует
                    русскую
                    лексику указанного периода и по возможности широко разрабатывает ее семантику.
                    Эта работа делается как для русской лексикографической науки, так и для высшей
                        школы, чтобы, как в хорошем ремесле, передавать опыт будущим поколениям.</p>

                    <p>НАЧАТЬ ОТ Остается напомнить, что еще в 1967 г., когда Сектор лингвистического
                    источниковедения и исследования памятников языка приступил к инвентаризации
                    источников, имеющихся в Институте русского языка, О.И.Смирновой была написана
                    статья о Картотеке ДРС, в которой подробно излагались сведения о составе ее
                    материалов, о состоянии карточек, перечислялись имена ее создателей (хотя и не
                    всех),
                    рассказывалось о 4-х этапах формирования картотеки до ее переезда в Москву,
                    давались образцы карточек А.И.Соболевского, М.Н.Сперанского, Б.А.Ларина,
                    П.К.Симони, образцы материалов однотомного словаря Д.И.Абрамовича (см.
                    Лингвистические источники. Фонды Института русского языка АН СССР. М. 1967. С.100-
                        124).</p>

                    <p>Автор настоящей работы опирается на материалы архива Группы, которые в 1989 г.
                    были частично переданы С.Ф.Геккер в Отдел исторической лексикологии и
                    лексикографии ИРЯ (Архив КДРС), а частично остался в Межкафедральном словарном
                    кабинете им. Б.А.Ларина Санкт-Петербургского университета. Это записка
                    "Исторический словарь", составленная, по-видимому, А.П.Евгеньевой (в машинописи
                    автор не указан), "Протокол обследования Картотеки ДРС в 1957 г.", "Итоги
                    обследования Картотеки Словаря древнерусского языка, проведенного Комиссией в
                    составе С.Г.Бархударова, Д.С.Лихачева, Б.А.Ларина, Р.И.Аванесова, А.П.Евгеньевой,
                    С.И.Коткова (председатель), В.Г.Орловой и Е.Н.Прокопович (секретарь)", различные
                    приказы и распоряжения, планы и отчеты, протоколы заседаний редколлегии и отдела
                    и
                    др. документы. Привлекаются материалы архива Академии наук в Москве и
                        Петербурге.</p>

                    <p>В конце 80-х годов появляются в некоторых сборниках статьи Л.Ю.Астахиной о
                    Б.А.Ларине, статьи, касающиеся роли материалов Картотеки ДРС для лингвистических
                    и иных историко-культурных исследований, раскрывающих ее историю, судьбу ее
                    создателей, ее лингвистическую содержательность и значение для современной
                    русистики. В газете Ростовского университета написала о Картотеке ДРС доцент
                    Л.В.Табаченко в связи с нашим поиском сведений о двух студентках Вирновской Т.В. и
                    Хоцяновой Н.И. из Ростова-на-Дону, которые принимали участие в пополнении
                        картотеки еще в довоенные годы.</p>

                    <p>В настоящее время коллектив Отдела исторической лексикологии и лексикографии
                    ИРЯ озабочен продолжением составления на материалах КДРС Словаря русского
                    языка XI-XVII вв. и вопросами сохранения уникального фонда картотеки для будущих
                        поколений ученых.</p>

                    <p>Предполагается при содействии ЮНЕСКО перевести эти материалы на компьютерные
                    носители, размножить их на лазерных дисках, отправить в крупные научные центры
                    нашей страны и зарубежные, для того чтобы исследователи могли не предпринимать
                    долгого путешествия в Москву, а иметь возможность работать с материалами
                        Картотеки ДРС на местах.</p>

                    <p>* * *</p>

                    <p>Картотека ДРС - это уникальное явление, национальное достояние страны. Ее
                    сокровища открыты всем: за последние 20 лет она около 4000 раз предоставляла свои
                    фонды исследователям из нашей страны и из-за рубежа. Есть в ней картотека учета
                    разрабатываемых на ее материалах тем, Книга учета посещений, Книга отзывов, в
                    которую особенно эмоциональные исследователи пишут стихи, так же, как, например в
                        1940 г. редактор ДРС Л.В.Успенский, ныне - известный писатель, написал "Гимн ДРС".</p>

                    <p>Картотека живет и помогает людям. В связи с разработкой в Российском парламенте
                    Закона об интеллектуальной собственности, а также в связи с переводом материалов
                    картотеки на новые современные носители, встал вопрос: чья это картотека, кому она
                    принадлежит? Ответ один: тому, кто ее создавал. А кто эти люди? Как сложилась их
                        жизнь? Какая "им досталась доля"?..</p>

                    <p>Вначале было намерение ограничиться несколькими словами о специализации и
                    научной деятельности каждого из тех, кто наполнял историко-лингвистическим
                    содержанием картотеку. Но когда пришлось обратиться к архивам ИЯМ, ГИМ, ГБЛ,
                    ГПБ, ЛОИИ, ИРЯ, ИЯ, РГАЛИ и др., открылись важные, порой трагические
                    обстоятельства в судьбе картотеки и ее создателей, связанные с жизнью страны, о
                    которых сегодня умалчивать нельзя. В самом деле: Картотека ДРС существует 70 лет,
                    а
                    единственный словарь на ее материале - Словарь русского языка XI-XVII вв. - начал
                        выходить только в год ее 50-летия, в 1975 г. На это должны быть серьезные причины.</p>

                    <p>Однако материалы архивов все же неполны и для нашей задачи оказались пригодными
                    лишь частично. В фонде Института языка и мышления (ф.77) Петербургского отделения
                    Архива РАН опись N 5 содержит сведения о выборщиках ДРС до августа 1941 г. Из
                    "Тетради поступлений материалов в картотеку", которую завел Б.А.Ларин в 1934 г.,
                    можно получить самые краткие сведения о том, кто какой памятник расписал и какую
                    получил сумму. Правда, в его Дневнике до 1937 г. есть яркие, подробные записи,
                    отражающие повседневные заботы руководителя и его сотрудников. С этого можно
                        было начинать.</p>

                    <p>Работы последних лет "Репрессированная наука" (1991), первый том книги документов
                    "Академическое дело" (1993), посвященных академику С.Ф.Платонову, книга
                    Ф.Д.Ашнина и В.М.Алпатова "Дело славистов. 30-е годы" (1994), конференция "Из
                    истории отечественной филологической науки. 20-50 годы" ??? в ИЯ РАН (январь 1995
                    г.) и опубликованные тезисы прояснили многие обстоятельства, ранее вызывавшие
                    вопросы. Наступление на науку, и в частности на науку о древнем русском языке,
                    началось с закрытия Отделения русского языка и словесности. Известные ученые
                    академики А.И.Соболевский, В.М.Истрин, М.Н.Сперанский, М.Н.Розанов, доказывали
                    пагубность такой меры еще в 1923 г. Но всю филологию и историю объединили в одно
                    Отделение гуманитарных наук (позже - Отделение общественных наук), и языкознание
                        встало по алфавиту - на последнее место.</p>

                    <p>Работавшая в Академии наук в 1929 г. Комиссия по проверке кадров нанесла огромный
                    ущерб исторической лингвистике, беспощадно удаляя из академических учреждений
                    ученых непролетарского происхождения, тех, кто получил образование в духовных
                    академиях, семинариях, а тем более - в них преподавал, не считаясь с их знаниями,
                    опытом, квалификацией, стажем научной работы. Милостиво выдавала билет на Биржу
                    труда, как например, А.Н.Котовичу, или увольняла по 1 категории (т.е. без права
                    работать в государственных учреждениях), как Ф.И.Покровского. По делу академика
                    С.Ф. Платонова подверглись ссылке внештатные сотрудники ДРС С.Ф.Геккер и
                    Б.И.Коплан. С февраля 1934 г. началось "дело славистов", когда были оторваны от
                    своих научных занятий М.Н.Сперанский, А.Д.Седельников, А.М.Селищев,
                    Н.Л.Туницкий,
                    В.Ф.Ржига за якобы участие в "Российской национальной партии", за то, что
                    занимались славистикой... И уж в 1952 г. была уволена из ИЯ С.Л.Чернявская за
                    допущенные в словарных статьях ошибки: словарь легко критиковать, а
                    древнерусский
                        - тем более...</p>

                    <p>На протяжении шести десятилетий находилось много охотников командовать наукой. И
                    это удавалось. Возможно, и достижения наши потому столь скромны, что многие ученые
                    искренне откликались на эти команды, уверенные, что так надо. Другие же, хоть и
                    были
                    несогласны, тоже подчинялись, иначе можно было лишиться самого главного - права
                    работать. Трудно представить: из готового к печати многострадального первого тома
                    изымались словарные статьи согласно вновь установленным хронологическим
                    пределам, потом вливалось в него то, чего не было предусмотрено, затем делался из
                    двух и даже трех томов один... А ведь в 1946 г. на титульном листе пробного набора
                    "Исторического словаря XI-XVII вв." из тринадцати составителей и редакторов фамилии
                    семерых были в черных рамочках. Так авторские коллективы сообщают читателям о
                        своих горестных утратах...</p>

                    <p>Какое великое желание было у сотрудников Группы ДРС через создание
                    древнерусского словаря отдать свои знания народу, не получая ни титулов, ни званий
                    и
                        не ожидая этого...</p>

                    <p>О каждом из создателей в этой работе помещен материал, которым мы располагаем на
                    сегодня. Не полностью приводится и список их трудов: выбраны те работы, которые
                    ближе всего касаются лексикологии и лексикографии русского языка или смежных
                    дисциплин. Возможно, откроются другие архивы, а читателей заинтересует эта книга -
                    и
                        можно будет подробнее написать о тех, о ком сведения сейчас собирались по крохам.</p>

                    <p>* * *</p>

                    <p>...Цветы и листья привлекают взгляд, а о корнях вспоминают, когда растение
                    засыхает.
                    Но без корней - нет древа, особенно древа науки. Заслуживают нашей благодарной
