                    воспринимала перемены, происходившие в политической и общественной жизни
                        страны.</p>

                    <p>Конечно, какие-то шаги от неприязни к лойяльности руководство Академии делало:
                    убедившись в прочности новой власти, и по мере увеличения финансирования науки
                    академическое сообщество переходит к сотрудничеству с нею. В конце января 1918 г.
                    Наркомпрос направил непременному секретарю академику С.Ф.Ольденбургу
                    "основные
                    положения к проекту мобилизации науки для нужд государственного строительства"
                    (Г.А.Князев, А.В.Кольцов. Краткий очерк истории Академии наук СССР. М.-Л., 1957. --
                    С.69.
                    Далее - Князев). Общее собрание Академии приняло постановление, в котором
                    говорилось: "Российская Академия наук, главная цель которой -- содействовать
                    развитию
                    научного творчества, давно уже занята разработкой научных задач, тесно связанных с
                    благом России" (Протоколы заседаний Общего собрания РАН. 1918. Протокол N 22.
                    Приложение к протоколу заседания экстраординарного собрания 20 февр. 1918 г. - С.31.
                    -
                    Князев, с.69). В 1919 г. Совнарком принимает решение "Об улучшении положения
                    научных
                    специалистов" и создает Центральную комиссию по улучшению быта ученых (ЦЕКУБУ).
                    Правительство проявляет заботу о возвращении из Саратова эвакуированных во время
                    войны из Петрограда ценностей Академии: В.И.Ленин лично подписал мандат-
                    удостоверение чл.-корр. Вс.И.Срезневскому, ученому хранителю РО БАН, на право
                    вывоза
                    этих материалов. Ближайшим его помощником был Ф.И.Покровский. Им был
                        предоставлен особый товарный поезд с пассажирским вагоном и отрядом охраны.</p>

                    <p>При этом 20-е годы характеризуются нараставшим давлением властей на Академию,
                    стремлением подчинить ее и поставить на службу социалистическому строительству, и
                    в то
                    же время - "борьбой Академии за сохранение академических свобод и автономию,
                    против
                    государственного планирования и регламентирования научных исследований" (АД.1.
                        С.ХIII).</p>

                    <p>В 1923 г. в Москве Социалистическая академия была преобразована в
                    Коммунистическую
                    (Комакадемию), "структуру, главным образом, в области обществоведения, которая в
                    перспективе могла... поглотить... существующую Академию наук" (АД.1. С.XIV). Ей
                    противостояло и "Общество историков-марксистов", возглавляемое, как и
                    Комакадемия,
                    М.Н.Покровским. Институт истории РАНИОН в 1929 г. был слит с Комакадемией с той
                    целью, "чтобы он отделался от тех своих элементов, которые уже абсолютно никакому
                    использованию не подлежат", - так говорил М.Н.Покровский в своей речи "Институт
                    истории и задачи историков-марксистов" на заседании, посвященном этому событию
                    (Там
                        же).</p>

                    <p>За грядущими структурными изменениями и изменениями кадровой политики стояла
                    "большевизация науки". Именно общественные науки стали в 20-е годы ареной
                    всевозможных реформ и экспериментирования. "По убеждению М.Н.Покровского, это
                    прежде всего относится к историко-филологическому (III) отделению Академии... Нужно
                    или
                    радикально реорганизовать, и в смысле личного состава, и в отношении программы
                        занятий, гуманитарное отделение Академии, или вовсе его прикрыть" (АД.1. С.XV).</p>

                    <p>Радикальная реорганизация началась с идеи, высказанной еще в 1923 г., слить II и III
                    Отделения, уничтожив по существу Отделение русского языка и словесности. Это
                    вызвало
                    протест ученых. В их страстном письме 1923 г. "В защиту ОРЯС", хранящемся в
                    нескольких фондах СПб отделения Архива АН (Записка В.М.Истрина), читаем: "Надо
                    сказать, что момент для уничтожения под тем или другим именем, с одной стороны,
                    является самым подходящим. В настоящее время, когда вот уже шесть лет как
                    систематически уничтожается все русское, все национальное, как разрушаются все
                    культурные и духовные ценности тысячелетней русской исторической жизни, когда
                    русское
                    имя вычеркнуто даже из названия Русского государства (СССР), в такое время
                    исключение
                    из списков Академии наук "Русского" Отделения, несомненно, будет встречено в
                    известных сферах с нескрываемой радостью и удовольствием... Гражданский долг,
                    ответственность перед родиной, чувство национальной гордости, одним словом, все то,
                    что
                    известно под опасным в настоящее время словом "патриотизм", заставляет громко
                    протестовать против новой попытки еще в одном месте вычеркнуть русское имя... Наш
                    долг - указать власти на недопустимость нанесения такого оскорбления русскому
                    национальному чувству в мирной области наук" (ф.172, оп.1, ед.хр.13, л.77-78). Ученые
                    обращали внимание властей на то, что именно "через Отделение стали иначе уважать и
                    саму Академию наук ...Из всех трех отделений Академии Отделение русского языка и
                    словесности пользуется большею известностью, и, конечно, потому, что его
                    деятельность
                    затрагивает тот наиболее обширный круг русского интеллигентного общества, который
                    чувствует себя тесно связанным с русской землей, с русским народом, с русским
                    языком,
                        с русской литературой" (там же, л.70-71).</p>

                    <p>Этот протест ненадолго отодвинул решение о слиянии. Но группа академиков -
                    В.М.Истрин,
                    Н.К.Никольский, М.Н.Сперанский, П.А.Лавров, Б.М.Ляпунов, В.Н.Перетц,
                    А.И.Соболевский
                    и др. - оказалась на заметке, условия их жизни и работы ухудшились, хотя и раньше им
                    приходилось нелегко. Так, в письме А.И.Соболевскому от 21 янв. 1921 г.
                    М.Н.Сперанский
                    просил отрецензировать подготовленное В.Н.Щепкиным исследование о Палее. Вот
                    какой
                    он получил ответ: "Отвечаю, к сожалению, отказом. Не имея ни электр[ичества], ни
                    керос[ина], я по вечерам не занимаюсь, утро уходит на текущие дела и, так[им]
                    обр[азом], у
                        меня нет времени посмотреть "Пал[ею]" Щ[епкин]а" (ф.173, оп.1, ед.хр 227, л.13).</p>

                    <p>Трудно было и другим ученым. Так, помещение, где находились материалы, собранные
                    в
                    начале XX в. Н.К.Никольским и Ф.И.Покровским, еще в 1919 г. подверглось обстрелу,
                    что
                    привело в беспорядок картотеку, повредило списки произведений. И если в 1920-1924
                    гг.,
                    пока существовал Историко-библиографический музей славянорусской книжности при
                    РО
                    БАН, ему могли помочь сотрудники Н.Н.Зарубин и Н.А.Порфирьев, а также работавший
                    вне
                    штата В.М.Верюжский, то в 1924-1928 гг. Никольский продолжал упорядочивать и
                    пополнять ее один. Он классифицировал картотеку по разделам: 1) алфавитный список
                    авторов и их сочинений допетровской книжности с указанием рукописей, в которых они
                    находятся, 2) список пергаменных рукописей, 3) хронологический список
                    славянорусских
                    рукописей от XI до начала XVIII в., 4) алфавитный список владельцев рукописей, писцов,
                    переводчиков. "Будучи сдвинуты в один столбец, - писал Никольский, - карточки
                    занимают
                    25 метров" (Задачи...С. 15.). Перенумеровать и пересчитать их он не имел возможности.
                        Комиссия, проверявшая картотеку в 1947 г., насчитала в ней более 174 тысяч карточек.</p>

                    <p>В 1925 г. Российская Академия наук была преобразована в Академию наук СССР, а 18
                        июня 1927 г. Совнарком утвердил ее новый устав, заменивший Устав 1836 года.</p>

                    <p>Комиссия по составлению "Словаря русского языка" в 1925 г. была переименована, по
                    предложению В.М.Истрина, в Постоянную Словарную комиссию. Но в 1930 г.
                    прекратилось
                    издание Словаря русского языка, начатого еще Я.К.Гротом и продолженного
                    А.А.Шахматовым. Последним появился выпуск 2-й 9-го тома. Весь Словарь, выходивший
                    отдельными выпусками, обнимал отрезок "А - ОБКАТАТЬСЯ". В 1934-1937 гг. седьмым
                        изданием Словарь продвинулся до "ОБРАТНОСТЬ" и закончил свое существование.</p>

                    <p>Возможно, в такое трудное время и возникло у академика А.И.Соболевского опасение
                    за
                    свою богатую, собираемую годами картотеку выписок из опубликованных и особенно из
                    рукописных памятников, с которыми он, посещая архивы Москвы, продолжал работать
                    до
                    своей кончины. В таких условиях продолжение дела акад. И.И.Срезневского
                    становилось
                    первоочередной задачей. Само обоснование важности древнерусского словаря в тот
                    момент, когда в стране утверждалась идея будущей индустриализации, могло быть не
                    понято и требовало от А.И. Соболевского и М.Н.Сперанского известного мужества.</p>

                    <p>В Москве в это время интенсивно шло ручное расписывание памятников на узких
                    лоскутках бумаги, на газетных полях, оборотах плакатов, билетов, на любых листочках,
                    использованных когда-то с одной стороны. Никакой иной способ технически был тогда
                    невозможен. А время объединения сделанных выписок в общую картотеку и тем самым
                        создание базы для древнерусского словаря могло быть упущено.</p>

                    <p>В Докладной записке "О составлении словарей древнерусского и старорусского
                    языков" в
                    Отделение русского языка и словесности А.И.Соболевский писал 19 мая 1926 г.:
                    "После
                    выхода в свет последнего выпуска "Материалов" был поставлен вопрос об их
                    продолжении
                    во всех направлениях... Теперь...наступило время организовать словарные сборы,
                    придав
                    им возможно широкие размеры и возможно значительное разнообразие... Необходимо
                    образовать при ОРЯС специальную "Комиссию по собиранию словарных материалов" с
                    центром в Петербурге и с отделениями в Москве и других городах, где имеются
                        достаточные ученые силы" (ВЯ. 1960. N 2. - С.110).</p>

                    <p>Вместе с акад. М.Н.Сперанским, в то время директором Исторического музея и
                    заведующим Рукописным отделением, приняли участие в расписывании памятников его
                    коллеги: Н.П.Попов, Ф.В.Буслаев, И.М.Тарабрин, А.Д.Седельников, Н.Л.Туницкий,
                    П.А.Расторгуев, А.М.Селищев, М.Ф.Богданова, Т.А.Борзова, С.И.Соболевский,
                    В.Ф.Ржига.
                    В Петрограде начали делать выписки в рукописных отделах БАН и Публичной
                    библиотеки
                    Д.И.Абрамович, П.К.Симони, Вс.И. Срезневский, В.В.Майков, Б.В.Лавров и др., в Твери
                    и
                        позднее в Новгороде -- И.А.Виноградов.</p>

                    <p>А.И.Соболевский предлагал собирать материалы по следующим четырем группам: 1)
                    для
                    словаря древнего церковнославянского языка; 2) для словаря как
                    церковнославянского,
                    так и древнерусского языка, пополняющие "Материалы" Срезневского, т.е. словарные
                    извлечения из памятников русского письма, составленных или переведенных не
                    позднее
                    XIV в., по спискам XI-XVII столетий; 3) для словаря старого языка Московской Руси по
                    памятникам литературы, законодательства и делопроизводства, оригинальным и
                    переводным XV-XVII вв.; 4) для словаря старого языка Польско-Литовской Руси по
                    памятникам литературы, законодательства и делопроизводства, оригинальным и
                    переводным XIV-XVII вв. "Главное условие, - писал далее Соболевский, - строгий
                    порядок
                    работы. Хранение собранных словарных материалов должно быть сосредоточено в
                    Петербурге, в Академии наук, и потому - в Петербургский центр должны быть сдаваемы,
                    по
                    мере накопления, материалы отделений Комиссии. Обработка материалов должна
                    происходить также в Петербурге, чтобы было и достаточное однообразие и постоянная
                    возможность навести справку и познакомиться с данными. Конечно, ОРЯС должно
                    заботиться и о помещении материалов, и о работнике, подбирающем данные по
                    алфавиту и
                        по группам" (там же).</p>

                    <p>25 сентября 1925 г. при поддержке академиков В.М.Истрина, Е.Ф.Карского,
                    П.А.Лаврова,
                    Н.П.Лихачева, Б.М.Ляпунова, Н.К.Никольского была создана "Комиссия по собиранию
                    материалов по древнерусскому языку" под председательством академика
                        А.И.Соболевского.</p>

                    <p>Соболевский широко смотрел на задачи, стоящие перед ней: "Комиссия и ее отделения
                    работают не только над собиранием материалов (хотя собирание - главная их задача),
                    но
                    также и над обработкой этих материалов с точки зрения происхождения, звуковой
                    формы,
                    грамматических особенностей и - главным образом - значения" (там же). Поэтому они
                    должны иметь право "собираться в открытые и закрытые заседания, печатать
                    повестки,
                    проекты, протоколы и т.п., входить в сношения с учеными обществами и устанавливать
                    с
                    ними те или другие связи и соглашения. Комиссия сообщает своим отделениям и всем
                    ученым сведения о работах: где, кто над каким памятником работает, какие труды
                    подходящего содержания вышли в свет или готовятся к изданию, какие вопросы
                    затрудняют или замедляют работу, какие выступают на очередь и т.п. Комиссия и ее
                    отделения обязаны всеми своими средствами содействовать всякого рода
                        исследованиям, сообщая все данные, которыми располагают" (там же).</p>

                    <p>Но с самого начала Комиссия встречала различные препятствия в своей работе. Так,
                    например, А.И.Соболевский никак не мог добиться получения шкафа, где Комиссия
                    могла
                    бы хранить свои материалы: для этого ему пришлось три раза письменно обращаться
                    из
                    Москвы в Петроград. А вскоре (в 1927 г.) Отделение русского языка и словесности как
                    самостоятельная структура Российской Академии наук перестало существовать, что
                    надолго если не парализовало, то во всяком случае отодвинуло, оставило без должного
                        внимания работы по древнерусскому словарю.</p>

                    <p>Комиссия, возглавляемая А.И.Соболевским, устояла. Она вошла в состав Постоянной
                    Словарной комиссии в качестве ее подразделения. В 1928-1933 гг. ученым секретарем
                    этой Подкомиссии в Ленинграде был С.Г.Бархударов. Сбором и раскладкой материала
                    занималась Е.Н.Шипова. Именно о ее работе в письме к С.П.Обнорскому от 14 февр.
                    1933
                    г. писал М.Н.Сперанский: "Очень жалею, что не могу из Москвы помогать Е.Н.Шиповой
                    (кстати шлю ей привет): я готовился к сессии и имел доброе намерение облегчить,
                    сколько
                        сам разумею, дело, помогать ей" (СПб Отделение Архива РАН, ф.77, оп.1, N 16, л.2).</p>

                    <p>А.И.Соболевский, будучи председателем Подкомиссии по древнерусскому словарю и
                    находясь в Москве, в 1926-1929 гг. составлял списки источников, выборки из которых
                    были уже сделаны им самим и его коллегами. Он указывал полное название памятника
                    и
                    его сокращенное обозначение, встречающееся на карточках, место хранения
                    рукописей,
                    выходные данные публикаций, записывал количество карточек, а в конце отмечал,
                    когда
                    этот список вместе с карточками был отправлен в Академию наук в Петербург.
                    Сохранилась его записка от 1 мая 1926 г.: "В каждой первой пачке находится карточка,
                    с
                    полным наименованием источника (полное заглавие, название журнала и т.п.)". Эти его
                    списки вынесены на форзацы данной книги. Они были сдублированы в так называемой
                    "черной тетради" (тетрадь в черном переплете), где Соболевский к указанным выше
                        данным добавлял и фамилии тех, кто делал выборки.</p>

                    <p>Эти списки и "черная тетрадь" положили начало каталогам и указателю источников
                    будущей картотеки. Ныне тетрадь хранится в Межкафедральном словарном кабинете
                    им.
                    Б.А.Ларина СПб университета. Приведем образцы записей из нее: "N 262. Житие
                    Аврамия
                    Чухломск[ого]. Рум.муз. N 2496. Сокр[ащение] Авр.Ч. 120 к[арточек]". Выписки из этого
                    источника делал сам А.И.Соболевский. Еще одна запись: "N 265. Пачка извлечений из
                    Повести о Смутн[ом] врем[ени] по изд[анию] Археогр[афической] ком[иссии]. 3. При ней
                    реестр. Работа А.Д.Седельникова. 403 к[арточки]". В 1929 году за этими краткими
                        записями уже стояло около 100 тыс. карточек.</p>

                    <p>Сохранилась Записка А.И.Соболевского "Несколько мыслей о словаре древнего
                    церковнославянского языка", материалы к которому также предполагалось собирать в
                    рамках деятельности возглавляемой им Комиссии. В число источников такого словаря
                    должны войти, по его мнению, "все те русские тексты, которые дошли до нас в списках
                    не
                    позднее XIV в. и в которых нет признаков русского происхождения". Такие памятники в
                    то
                    время уже начал расписывать Н.Л.Туницкий, что было отмечено и в "черной тетради"
                        А.И.Соболевского.</p>

                    <p>Академик М.Н.Сперанский, активно включившийся в работу Комиссии (а с мая 1929 г.,
                    после кончины Соболевского, возглавивший ее), прежде всего поставил задачу
                    определить
                    круг источников, необходимых для создания будущего исторического словаря русского
                    языка. Еще в 1921 г. М.Н.Сперанским, как руководителем РО ГИМ, было предпринято
                    составление алфавитно-систематического каталога и постатейная роспись сборников,
                    входящих в различные собрания. В процессе этой работы проходил отбор памятников
                    как
                    для издания, так и для картотеки древнерусского словаря. Он упоминает "Русскую
                    грамматику" Г.В.Лудольфа, изданную в Оксфорде в 1696 г 1. 0, как расписанную для
                    КДРС
                    А.М.Селищевым. Впоследствии эта Грамматика стала одним из источников докторской
                    диссертации Б.А.Ларина, который перевел ее на русский язык, составил алфавитный
                        указатель и опубликовал вместе с латинским оригиналом в 1937 г.</p>

                    <p>Сам М.Н.Сперанский работал преимущественно с текстами рукописных памятников
                    Румянцевского и Исторического музеев. Им было написано для Картотеки более 36
                    тыс.
                    карточек по 127 источникам. О пополнении картотеки он пишет в письме к
                    С.П.Обнорскому
                    от 24 апр. 1933 г.: "Кстати, вот Вам отчет о сделанном и делаемом: пока жатва
                    невелика...
                    у меня в столе приготовлено к отправке для Елизаветы Николаевны [Шиповой -- Л.А.]
                    только 1300 карточек, да на днях ожидаю из Новагорода [так - Л.А.] от Виноградова
                    окончание его выборки из дела о Шакловитом. Так немного карточек набралось потому,
                    что я за это время возился с выборкой из рукописных текстов, при том с иноземными
                    оригиналами в руках, что значительно осложняет и писание карточек и замедляет
                    выборку,
                    т.к. приходится дело делать не дома, а в рукописных отделениях" (СПб Отделение
                    Архива
                    РАН, ф.77, оп.1, N 16, л.4). В сентябре он писал: "Летом послал я и тюк с готовыми
                    карточками Московского и Новгородского изготовления. надеюсь, они уже дошли до
                        Елизаветы Николаевны" (там же, л.6).</p>

                    <p>По просьбе Непременного секретаря Академии наук от 31 июля 1927 г.
                    С.Ф.Ольденбурга
                    М.Н.Сперанский написал "Записку о Комиссии по собиранию словарных материалов по
                    древнерусскому языку", о ее целях и деятельности. В ней говорилось: "Материалы"
                    Срезневского обнимают большею частию только древнейшие памятники русского
                    языка и
                    письма, кончая XV веком, редко переходя в XVI-й (главн. обр. памятники юридического
                    характера); самый XVI-ый век и XVII-ый недостаточно или вовсе не представлены своим
                    словарным материалом", хотя "со времени составления И.И.Срезневским его
                    "Материалов" количество как печатных изданий, так и известных рукописных текстов
                    значительно возросло, а самые требования от Словаря древнерусского языка также
                    значительно повысились" (ф.172, оп.1, ед.хр.13, л.88). " Древнерусский словарь,
                    считал
                    он,
                    "должен явиться необходимым пособием не только для специалистов по истории
                    русского
                    языка, но и для сравнительного изучения славянских языков, а сверх того,
                    практическим
                    пособием для всех, кому приходится заниматься с той или иной точки зрения русским
                    прошлым. Академическая комиссия, кроме того, должна явиться центром, в котором
                    сосредоточатся отдельные разобщенные труды по лексикологии старого русского
                    языка
                        во всем государстве" (там же).</p>

                    <p>В его Записке было не только описание процесса собирания древнерусского
                    материала,
                    но и предложение по научной систематизации и осмыслению собранного. Он предлагает
                    не
                    ограничиваться лишь старшим периодом русской письменности, а привлечь материал
                    XVI и
                    XVII вв. как времени, "когда под постоянно усиливавшимся влиянием Запада самый
                    лексический состав древнерусского языка стал испытывать значительные изменения"
                        (там же).</p>

                    <p>Рабочие моменты описываются в Записке Сперанского так: "Отдельными
                    сотрудниками
                    берутся отдельные рукописные, печатные (из не вполне использованных
                    И.И.Срезневским)
                    или рукописные тексты, впервые привлекаемые, древнерусских памятников, из них
                    выбирается словарный материал в виде отдельных слов с контекстом, в которых они
                    встречены, с указанием значения слова на иностранном языке (если памятник
                    переводный
                    [так - Л.А.] или значение слова может быть точно определено на тех или иных
                    основаниях).
                    Выбранные т[аким] о[бразом] слова пишутся на отдельных карточках с точным
                    (условным
                    для краткости) обозначением источника (название текста, место его хранения, номер
                    страницы печатного издания или лист рукописи) и упакованные - каждый т[аким]
                    о[бразом]
                    разработанный памятник отдельно - сдаются по мере накопления для хранения в
                    помещение Комиссии (общем с Постоянной Словарной комиссией). Определение, из
                    какого памятника должна происходить выборка слов, делается или в заседаниях
                    Комиссии, или по указанию или с согласия председателя Комиссии, через которого
                        готовый материал передается и для хранения в Комиссии.</p>

                    <p>В настоящее время идет усиленное лишь накопление материала, подвигающееся
                    вполне
                    успешно; но приступить к дальнейшей обработке, главн[ым] образом сводке материала,
                    Комиссия признает еще преждевременным. Комиссия начала свою работу только около
                    двух лет тому назад. В дальнейшем с развитием состава Комиссии (в ее состав входят
                    главным образом ученые московские) желательна большая систематизация в подборке
                    памятников по их жанрам и характеру (юрид[ический] акт, повествоват[ельный]
                    памятник,
                    поэтический, научный и т.п.), чем до некоторой степени обусловливается их
                    лексический
                    состав. Сверх того весьма желательно для Комиссии иметь небольшое хотя бы
                    количество
                    печатных листов в том или ином издании Академии наук, чтобы она имела возможность
                    опубликовывать те небольшие по объему, но ценные в научном отношении
                    исследования по
                    отдельным вопросам истории языка, в частности истории русского языка, в частности,
                    исторической его лексикологии, которые служат предметом докладов в заседаниях
                    Комиссии. Сколько мне известно, подобной работы не производилось ни одним из
                        существующих вне Академии научным учреждением" (там же, л.88-89).</p>

                    <p>По этим и другим косвенным свидетельствам можно заключить, что при начале
                    собирания
                    карточек по четырем направлениям появилась и вторая, отличная от точки зрения
                    А.И.Соболевского, концепция древнерусского словаря. В фонде М.Н.Сперанского
                    сохранилось извещение о том, что академик А.И.Соболевский делает доклад "Два
                    проекта
                    словаря древнерусского языка", однако без имени авторов проектов (СПб Отделение
                        Архива РАН, ф. 172, оп.1, ед.хр. 13, л.82).</p>

                    <p>Готовя к очередному заседанию образцовую страничку словаря, в одном из писем
                    М.Н.Сперанский обращается к С.П.Обнорскому, как знатоку словарного дела и как
                    секретарю Постоянной Словарной комиссии: "Вас, как самого между нами опытного по
                    этой части, прошу помочь нам, набросавши проект или странички, или указавши то, что
                    и
                    как надо будет заносить в словарь. Ваш и мой проект могли бы подвергнуться
                    обсуждению
                    в заседании; таким образом мы бы сдвинулись с мертвой точки". Сам Сперанский
                    сделал
                    полную выборку из "Повести о Царьграде", как он писал, "но в отдельных мелочах не
                    уверен, так ли я это сделал. Думаю, что хорошо бы было выработать коротенькие
                    правила
                    для такой выборки" (Письмо от 24 сентября 1933 г.). Обнорский принял живейшее
                    участие
                        в составлении инструкции для выборщиков.</p>

                    <p>В 1929 г. Постоянная Словарная комиссия (с включенной в нее ранее Комиссией,
                    возглавляемой А.И.Соболевским, затем - М.Н.Сперанским) вместе с Комиссией по
                    диалектологии русского языка образовала единую Комиссию по русскому языку (КРЯЗ)
                    под председательством акад. Е.Ф.Карского (затем - Н.С.Державина). Противостояние
                    ученых и власти продолжалось: организацию гуманитарных институтов решено было не
                        развертывать (АД.1. С.ХХII).</p>

                    <p>Комиссия по проверке кадров Академии наук под председательством Ю.П.Фигатнера с
                    участием представителей профсоюза работников просвещения, Рабоче-Крестьянской
                    инспекции приступила к работе в 1929 г. Всего по Академии было уволено 128 человек
                    из
                    960 штатных сотрудников и 520 из 830 сверхштатных. Комиссия удаляла из
                    академических
                    учреждений ученых непролетарского происхождения, лиц, закончивших духовные
                    учебные
                    заведения, не считаясь с их знаниями и опытом. Так, был уволен и арестован
                    Ф.П.Покровский, ученый хранитель Рукописного Отделения БАН: ему
                    "инкриминировалось
                    участие в сокрытии от Советского правительства обнаруженных в трех академических
                    учреждениях архивных фондов актуального государственного значения" (АД.1. С.VIII).
                    Члена Археографической комиссии историка М.Д.Приселкова обвинили в том, что
                    будучи
                    преподавателями университета, они вместе с Тхоржевским "создали нелегальные
                    кружки,
                        в которых подготовляли кадры антисоветских научных работников" (там же, с.XII).</p>

                    <p>В январе 1930 г. постоянный представитель ОГПУ по Ленинградской области
                    санкционировал арест группы известных ученых, принадлежащих к старой школе
                    русских
                    историков. Официальная версия гласила: подозрение в активной антисоветской
                    деятельности и участие в контрреволюционной монархической организации. Так
                    возникло
                    "дело Академии наук". Подробнее это описано в статье В.А.Колобкова "Сергей
                    Платонов:
                    год накануне ареста" (Источниковедческое изучение памятников письменной культуры
                    в
                    собраниях и архивах ГПБ. История России XIX-XX вв. Сб. научных трудов. Л. 1991.
                    С.155-
                        174) и в 1-м томе книги "Академическое дело", посвященном С.Ф.Платонову.</p>

                    <p>С.Ф.Платонов окончил историко-филологический факультет Петербургского
                    университета
                    и был оставлен для подготовки к профессорской деятельности. В 1903 г. он возглавил
                    Петербургский Высший педагогический институт, вскоре был приглашен в качестве
                    преподавателя в императорскую семью. В 1919 г. он стал председателем
                    Археографической комиссии, в 1920 г. - действительным членом Академии наук по III
                    Отделению исторических наук и филологии. С 1928 г. он стоял во главе Отделения
                    гуманитарных наук, которое было образовано согласно новому Уставу 1927 г.: "Вместо
                    трех отделений было образовано два: Отделение математических и естественных наук
                    и
                    Отделение гуманитарных наук, объединившее прежние Историко-филологическое
                        отделение и Отделение русского языка и словесности" (Князев, с.85).</p>

                    <p>Когда 12 января 1928 г. тайным голосованием были отвергнуты на выборах в Академию
                    три кандидатуры (А.М.Деборин, Н.М.Лукин и В.М.Фриче), Непременный секретарь АН
                    С.Ф.Ольденбург предложил просить Совнарком СССР о разрешении провести повторное
                    голосование в конце января, хотя это было прямым нарушением Устава Академии наук.
                    В
                    газетах стали появляться статьи с обвинением академиков в контрреволюционных
                    убеждениях (Известия 5 февр., Ленинградская правда 6 февр., 19 февр. 1929 г.),
                    вышла
                    книга "Классовый враг на историческом фронте" (авторы - Г.С.Зайдель и М.М.Цвибак. -
                        АД.1. С.ХLV).</p>

                    <p>Кроме этого, причиной возникновения "дела Академии наук" были раскопки в Крыму
                    древнего пещерного городища Эске-Кермен недалеко от Бахчисарая, в которых
                    готовились
                    принять участие представители германской науки, изучавшие готские древности и
                        обещавшие материальную помощь.</p>

                    <p>Еще одной причиной было сообщение "коммунистов Центрархива" членам
                    правительственной Комиссии по проверке аппарата АН СССР под рук. Ю.П.Фигатнера о
                    том, что в Академии тайно хранятся документы "большого политического значения".
                    Среди
                    этих документов оказался запечатанный конверт с подлинником отречения от престола
                    Николая II и его брата Михаила, бумаги Департамента полиции, корпуса жандармов,
                    Охранного отделения и др. Обвинение состояло в том, что Отделение, руководимое
                    С.Ф.Платоновым, сознательно препятствовало передаче их в Центральный архив
                    РСФСР,
                        возглавляемый М.Н.Покровским.</p>

                    <p>Постепенно выяснилось, что архив корпуса жандармов и Департамента полиции
                    передал в
                    Центрархив академик А.А.Шахматов еще в 1917 г., что бывший шеф жандармов
                    генерал
                    В.Ф.Джунковский имел разрешение работать с документами личного архива
                    (переданного
                    еще в сентябре 1915 г.) до своей смерти, что эти и другие документы хранились
                    открыто.
                    Но кандидатура академика С.Ф.Платонова, создателя крупной научной исторической
                    школы, имевшего связи с заграницей, оказалась подходящей для роли руководителя
                    заговора и контрреволюционной организации, которая в ОГПУ была названа
                        "Всенародный союз борьбы за возрождение свободной России".</p>

                    <p>12 янв. 1930 г. Платонов был арестован. По "делу Платонова" были привлечены
                    историки
                    Б.А.Романов, Н.С.Чаев, Б.Д.Греков и П.А.Садиков, археографы Вс.И.Срезневский и
                    Ф.И.Покровский, литераторы Б.И.Коплан и Е.П.Казанович, библиограф С.Ф.Геккер и
                    др.,
                    участвовавшие впоследствии в создании Картотеки ДРС. Ученый секретарь
                    Археографической комиссии А.И.Андреев был арестован еще в конце октября 1929 г.,
                    хотя
                    именно он в составе комиссии, проверявшей РО БАН еще в марте, написал списки
                        хранящихся в Отделении документов.</p>

                    <p>Как говорилось в Докладной записке вице-президента АН А.Е.Ферсмана, вина
                    С.Ф.Платонова заключалась в том, что "вопрос был ошибочно поставлен в плоскость
                    формальных взаимоотношений и споров с другими хранилищами, но не по существу
                        хранившихся документов, их актуальности и важности" (Колобков, с.171).</p>

                    <p>В 1930 г. Археографическая комиссия была объединена с Институтом истории
                    (Ленинградское отделение). Происходили и другие преобразования в Отделении
                    гуманитарных наук после ареста его главы С.Ф.Платонова. В том же году была
                    упразднена
                    Комиссия по русскому языку в связи с открытием на базе Института
                    яфетидологических
                    изысканий РАН (существовавшего с 1921 г. и некоторое время называвшегося
                    Яфетическим институтом) Института языка и мышления (СПб. Отд. Архива РАН, ф.1,
                    оп.1,
                    ед.хр.25; ф.2, оп.1, ед.хр.31, 128. 1929-1930 гг.). В ИЯМ был создан Отдел по изучению
                    русского языка. В него, в Словарный отдел, вместе с секцией Словаря [современного]
                    русского языка, также на правах секции, в 1934 г. вошла Группа древнерусского
                    словаря
                        (ДРС).</p>

                    <p>В 1932 г. была выделена одна штатная единица для ученого секретаря Комиссии,
                    которую
                    занимал С.Г.Бархударов. В 1934 г. ученым секретарем Секции ДРС стал Б.А.Ларин. О
                    нем
                    еще в 1933 г. в письме к Обнорскому М.Н.Сперанский писал: "Б.А.Ларина я не знаю и
                    буду
                    рад с ним познакомиться в надежде привлечь его к делу" (СПб Отделение Архива РАН,
                        ф.77, оп.1, N 16, л.4).</p>

                    <p>Однако работать вместе им пришлось недолго. По обвинению в создании "Российской
                    национальной партии", в пропаганде славянской филологии, которая в обвинительном
                    акте
                    квалифицировалась как "реакционная наука, распространенная в фашистской
                    Германии",
                    в подготовке контрреволюционного заговора были арестованы (начиная с 7 февр. 1934
                    г.)
                    А.М.Селищев, Г.А.Ильинский, Н.Н.Дурново и его сын Андрей, В.В.Виноградов,
                    Н.Л.Туницкий, А.Д.Седельников, В.Ф.Ржига и др. В ночь с 11 на 12 апреля 1934 г. в
                    Москве
                    был арестован академик М.Н.Сперанский, а в Ленинграде - академик В.Н.Перетц.
                        Начиналось так называемое "дело славистов".</p>

                    <p>М.Н.Сперанский окончил в 1885 г. словесное отделение историко-филологического
                    факультета Московского университета и был оставлен для подготовки к
                    профессорскому
                    званию. В 1890-1892 гг. находился в заграничной командировке в Праге, Вене, Загребе
                    и
                    других городах, где знакомился с рукописными памятниками, написанными на русском
                    и
                    славянских языках. В 1895 г. защитил магистерскую диссертацию "Славянские
                    апокрифические евангелия", а в 1899 г. - докторскую диссертацию "Из истории
                    отреченных
                        книг".</p>

                    <p>В 1895-1906 гг. он преподавал в Историко-филологическом институте в Нежине и затем
                    - в
                    Киеве, а в 1906 г. вернулся в Москву. В 1902 г. он стал членом-корреспондентом
                    Академии
                    наук, а в 1921 г. - действительным членом. Широко был известен его курс "История
                    древней русской литературы", изданный трижды в 1914-1921 гг. Занимался он также
                    русско-южнославянскими литературными связями, историей русской и зарубежной
                        славистики, русской литературой XIX в. и другими научными проблемами.</p>

                    <p>Начиная с 1906 г., по понедельникам в московской холостяцкой квартире
                    М.Н.Сперанского
                    собирались его слушатели, друзья и ученики. До конца 20-х годов он руководил
                        Рукописным отделением Исторического музея.</p>

                    <p>Отношения Советского Союза в 30-е годы со славянскими странами были крайне
                    напряженными. В науке шла борьба с "панславизмом", отрицалось общее
                    происхождение
                    славянских языков и народов. А для начинающегося "дела славистов" необходима была
                    крупная фигура, имеющая заграничные связи, чтобы можно было поставить ее во главе
                    этого "заговора". Был избран академик М.Н.Сперанский, которому был 71 год. Летом
                    1934
                    г. дважды ему выносили приговор: 16 июня - 3 года ссылки в Уфу, а 17 ноября Особое
                    совещание комиссии НКВД постановило считать приговор условным. 2 декабря 1934 г.
                    Сперанский был лишен звания академика Общим собранием АН, затем в течение 4-х
                    лет
                    оставался как бы под домашним арестом. Но он продолжал заниматься своей наукой и
                    подготовил 4 крупных работы. В том же 1934 г. вышла публикация М.Н.Сперанского "Из
                    старинной новгородской литературы ХIV в." Это был 4-й и последний выпуск серии
                    "Памятники древнерусской литературы", подготовленный в рамках деятельности
                    Комиссии по изданию памятников древнерусской литературы (КПДЛ). В ее работе с
                    1906 г.
                    принимал участие и М.Н.Сперанский. Скончался он ровно через 4 года после ареста.
                    Подробности этого дела описаны в книге Ф.Д.Ашнина и В.М.Алпатова "Дело славистов.
                        30-е годы". (М. 1994).</p>

                    <p>25 апреля 1934 г. Академия наук была переведена в Москву.</p>

                    <p>В 1934 г. у Б.А.Ларина с М.Н. Сперанским шла деловая переписка. Будучи секретарем
                    Комиссии и фактически - главой Группы ДРС в Ленинграде, каждый шаг которого
                    требовал
                    в таких условиях фиксации и отчетности, он начал вести "Дневник занятий в Комиссии
                    Словаря русского языка XV-XVIII вв. при ИЯМ его сотрудников" (далее -- Дневник).
                    Первая
                    запись сделана 20 февр. 1934 г.: "Принял от С.Г.Бархударова имущество Комиссии
                    (карточки, книги, бланки, переписку, протоколы и проч. материалы). Ознакомился с
                    делами
                    Ком[исс]ии. Составил акт передачи. Снял копию годового плана Комиссии для отправки
                        ак[адемику] Сперанскому". Записи в дневнике были подробными вплоть до лета 1937 г.</p>

                    <p>Одновременно с Дневником Ларин завел "Тетрадь для записи поступления материалов
                    для Словаря древнерусского языка и отметки выдач материала для исправлений"
                    (далее -
                    Тетрадь поступлений). Эту тетрадь вели до 17 авг. 1941 г. сначала Б.А.Ларин, затем
                    его
                    внештатные сотрудники Е.П.Динариева, Е.М.Иссерлин, Н.А.Ульянова, С.Л.Чернявская и
                    А.И.Терновская. Как видим, в Дневнике и Тетради поступлений словарь назван по-
                    разному. Со временем было принято решение о составлении "Словаря древнерусского
                        языка ХV-XVIII вв." в 8 томах по 100 печатных листов каждый.</p>

                    <p>В Записке "Исторический словарь" читаем: "Первые пачки выписок из древнерусских
                    текстов поступили в Академию наук от академика А.И.Соболевского в мае 1926 г. ...До
                    своей смерти он успел заготовить вместе со своими сотрудниками около 100 000
                    выписок
                    в 2-3 слова каждая, а нередко и в одно слово. Эти материалы послужили в дальнейшем
                    сигналами при отыскании вполне пригодных для Словаря цитат" (Архив КДРС, папка N
                    7,
                        док. N 13. С. 2-3).</p>

                    <p>Б.А.Ларин и Е.Н.Шипова в конце марта -- начале апреля 1934 г. трижды пересчитали по
                    документам поступлений количество карточек. Получили три различные величины:
                    146741,
                    144945 и 145538. Б.А.Ларин записал в Тетради поступлений эти данные, сопроводив их
                    следующим пояснением: "Расхождение подсчетов объясняется целым рядом
                    сомнительных или неточных указаний числа карточек в отдельных пачках.
                    Сомнительные
                    случаи, очевидно, были включены в счет каждый раз по-разному" (Запись 1 апр. 1934
                        г.).</p>

                    <p>Ознакомившись пристальнее с карточками, переданными ему С.Г.Бархударовым,
                    Б.А.Ларин понял, что А.И.Соболевский и его коллеги в Москве выписывали для своей
                    работы слишком короткий текст из источников, что совершенно не подходило для
                    создания
                    словаря. Эти карточки получили название "дефектных", и в первые месяцы 1934 г.
                    внештатные сотрудники были заняты "выявлением и исправлением" этих карточек, т.е.
                    переписыванием их по указанным источникам с расширением прежнего минимального
                    контекста. Из 604 пачек таких карточек 314 было переработано за 1934 год; остальные
                    290
                    пачек были розданы для исправления, дополнения и замены по источникам. За эту
                    работу
                    выборщики, как их стали называть, получали по 10 коп. за карточку. Столько же
                    платили и
                    за выборку из любого нового источника, независимо от того, была ли это публикация
                    или
                        рукопись - уставная, полууставная, скорописная.</p>

                    <p>Не всеми сотрудниками эта работа была встречена с пониманием, не все работали
                    аккуратно и внимательно. И тогда в группе была заведена особая тетрадь "Полынь-
                    трава
                    ДРС", в которой обыгрывались всякого рода промахи: когда вместо одной "дефектной"
                    карточки появлялись две, если выборщик поскупился и недостаточно расширил
                    контекст;
                    когда выборщик забыл поставить в конце цитаты шифр источника и указать страницу,
                    и
                    пр.
                    Тетрадь вела Е.М.Иссерлин. Иногда Б.А.Ларин, А.П.Конусов или кто-то другой "давали
                    ответ", указывая шифр или страницу источника: сохранились карточки с их ответами в
                    этой
                        тетради.</p>

                    <p>С 1934 г. долгое время Б.А.Ларин был единственным штатным сотрудником в Группе
                    ДРС.
                    Постепенно он сумел объединить вокруг этой работы молодых энтузиастов, горячих
                    сторонников идеи создания древнерусского словаря и базы для него, среди которых
                    "витал дух свободного исследования" (как сказал бы М.Е.Салтыков-Щедрин) и
                    подвижничества. Среди преподавателей, хранителей рукописных фондов библиотек,
                    аспирантов было много подлинных знатоков и любителей древнерусской литературы и
                    письменности, которых Б.А.Ларин привлек к делу создания картотеки как внештатных
                    выборщиков. Много сил и энергии он отдавал, занимаясь не только организационными
                    вопросами, но и серьезным научным осмыслением этой работы на основами будущего
                    словаря. Проблематика Института языка и мышления, теория стадиального развития
                    языка акад. Н.Я.Марра, связь с историей материальной культуры, социальными
                    противоречиями, когда язык объявлялся средством "для вскрытия классовых
                    отношений"-
                        , все это требовало разумной реакции.</p>

                    <p>Ознакомившись с докладными записками А.И.Соболевского и М.Н.Сперанского,
                    Б.А.Ларин
                    сосредоточил свои усилия на теоретическом обосновании работы над словарем, на
                    составлении проекта древнерусского словаря XV-XVIII вв. как на задаче
                    первостепенной.
                    Посылая отчеты М.Н.Сперанскому в Москву, получая от него материалы для картотеки
                    (последние пришли в 1936 г.), он на протяжении 1934-1935 гг. разрабатывал основы
                    будущего словаря "между принципиальными спорами и малыми будничными делами по
                        уточнению и улучшению деталей в самой технике подготовки... материалов".</p>

                    <p>Как он напишет в своем "Проекте", "фундаментальная перестройка производится нами
                    теперь, по мере того, как удается на деле провести в словарной работе принципы
                    нового
                    учения о языке... чтобы вскрыть стадиальные отношения в семантике слова, раскрыть
                    историю - языковой процесс во всей конкретности" (с.6-7). Тогда казалось, что именно
                    это
                    необходимо и возможно сделать. Смягчая формулировку о классовости подхода
                    "нового
                    учения о языке" академика Н.Я.Марра, Б.А.Ларин мыслил о создании "глубокой
                    социальной перспективы в ДРС", обдумывал необходимость для этого "обогащения
                    ДРС
                    реально-историческими комментариями, иллюстрациями, диалектологическими
                    материалами в записях XIX века" (там же). Именно в "синтезе узко-филологического
                    словаря с историко-терминологическим и со словарем культурных реалий" Б.А.Ларин
                        видел "новый этап в области исторической лексикографии" (с. ???).</p>

                    <p>"Проект древнерусского словаря. Принципы, инструкции, источники" Б.А.Ларина вышел
                    из
                    печати в 1936 г. в Ленинграде. В архиве М.Н.Сперанского есть визированная им
                    корректура этой книги. Возможно, в связи с арестом переправить ее Б.А.Ларину ему не
                    удалось. Да и имени его в "Проекте" не упоминалось, говорилось о "преемнике
                        А.И.Соболевского".</p>

                    <p>"Мы делаем необычную в филологии попытку привлечь широкие научные круги к
                    обсуждению проекта Древнерусского словаря раньше, - писал Б.А.Ларин в "Проекте", -
                    чем
                    начата работа по его составлению, когда заканчивается только сбор и упорядочение
                    сырых
                    материалов и подготовляются первые кадры работников, когда еще очень многое и
                        существенное можно изменить в плане этой работы" (с.10).</p>

                    <p>"Основная цель ДРС, - считал Б.А.Ларин, - разъяснить на большом достоверном и
                    хронологически определенном материале историю значений и фразеологические
                    употребления слов в русском литературном языке и в русских диалектах с XV по
                    середину
                        XVIII в.</p>

                    <p>...В словарь вводятся не только узко-литературные материалы, но используются и
                    деловые
                    документы, и фольклор, и специальная литература и диалекты; даются не только
                    филологические, но и реальные толкования слов... вводятся иллюстрации; отражены
                    будут
                    и результаты специальных семасиологических исследований (по отдельным словарным
                        темам), предпринятых сотрудниками ДРС" (Проект, с. 42).</p>

                    <p>Картотеку, по его мысли, нужно было довести до 1 млн карточек и на этой базе
                    составлять
                    исторический "Словарь древнерусского языка XV-XVIII вв." Из материалов XVIII в.
                    необходимо включить в картотеку деловые документы, эпистолярные материалы, язык
                    которых еще не был изучен. Лексика художественных произведений, так называемой
                    "большой литературы" XVIII в., уже была включена в Картотеку современного языка,
                    на
                    основе которой в 1891 г. Комиссия по составлению Словаря русского языка начала
                    выпускать словарь под редакцией академика Я.К.Грота, а позднее - под редакцией
                    академика А.А. Шахматова. Картотеки эти, по мнению Б.А.Ларина, должны были
                        сомкнуться.</p>

                    <p>В Картотеку ДРС необходимо было брать и так называемые фоновые материалы:
                    записи
                    былин и народных песен, записи местных говоров, сделанные в XIX в., данные
                    различных
                    словников и словарей, особенно диалектных. В "Проекте" Б.А.Ларин обосновывал
                    "методологическую необходимость нераздельного изучения языка и реалий, языка и
                    идеологий" (с.16), а потому - и привлечения к словарному делу "данных истории
                    материальной культуры", хозяйства, "общественного строя и истории мировоззрений
                    русского средневековья" (с.15). А для этого необходимо было вовлечение "в словарное
                        дело специалистов по русским древностям, быту и материальной культуре" (с.16).</p>

                    <p>Непременным элементом словаря Б.А.Ларин считал иллюстрации, так как "для
                    некоторых
                    идей и представлений, связанных со словами, зрительный образ вещи является их
                    основным, бесспорным или даже единственным содержанием" (с.19), а символическое
                    изображение для других представлений "может быть таким же важным и опорным, как
                    образ вещи" (там же). Иллюстрации предполагалось извлекать из так называемых
                    "лицевых" (иллюстрированных) рукописей, где изображение часто прямо соответствует
                    описанию, либо подбирать в музеях древностей, на иконах, в этнографических
                    работах.
                    Но
                    главным Б.А.Ларин считал "схематические графические воспроизведения вещей,
                    действий
                    по изображениям" (с.22). Предполагалось давать и "сводные таблицы, где собраны
                    будут
                    изображения различных реалий одного слова (напр., повозок, кораблей, шапок,
                    пищалей и
                        т.п.)" и многокрасочные миниатюры (с.22).</p>

                    <p>"С фондом около миллиона карточек из нескольких тысяч источников, - писал далее
                    Б.А.Ларин, - мы приступим к изданию словаря в 8 томах по 10 выпусков, свыше 800
                    печатных листов. Он будет содержать около 150 тысяч словарных статей. Закончить
                    издание предполагаем к началу 1945 г." (с.15). В обсуждении отдельных частей
                    Проекта
                    деятельное участие принимали Б.М.Ляпунов, Л.В.Щерба, С.П.Обнорский,
                    В.И.Чернышев,
                        П.К.Симони, Е.С.Истрина и др.</p>

                    <p>В 1934 г. началось не только исправление так называемых "дефектных" карточек, шла
                    выборка и из вновь привлекаемых источников. В качестве выборщиков работали
                    В.И.Бантышев, Я.Я.Горунович, Е.М.Иссерлин, Б.Л.Богородский, А.П.Конусов,
                    А.Н.Котович,
                    Б.В.Лавров, В.В.Майков, П.К.Симони, А.П.Евгеньева, Е.Н.Шипова, Л.В.Успенский,
                        Р.С.Лисовская, И.М.Калинин, М.А.Полторацкая и Б.А.Ларин.</p>

                    <p>Раскладку делали Е.П.Динариева, Е.Н.Шипова и Н.М.Лысс. Они довели ее до конца
                    алфавита. А.П.Конусовым, Е.М.Иссерлин и Б.В.Лавровым было начато составление
                    словника Картотеки ДРС (буквы А-Б). В том же году была составлена и утверждена
                    Инструкция по первичной обработке фонда, собирался материал по редактированию
                        словаря.</p>

                    <p>В феврале 1934 г. Б.А.Ларин начал составление трех каталогов: алфавитного каталога
                    полных наименований источников, каталога сокращенных наименований и обозначений,
                    принятых в Картотеке ДРС и долженствующих войти в самый словарь при его издании,
                    и
                    каталога имен авторов, издателей и заглавных имен. Кроме того, была начата
                    подготовка
                    редакторских кадров среди сотрудников Отделения, которое с 1930 г. называлось
                    Отделением общественных наук (ООН). Начали составлять пробные словарные статьи
                    Б.А.Ларин, А.П.Евгеньева, Е.М.Иссерлин, А.П.Конусов, Л.В.Успенский, А.Н.Котович и
                    Б.В.Лавров. Уже 22 ноября 1934 г. в Группе ДРС обсуждалась первая пробная
                    словарная
                        статья на слово 1пирог 0, составленная Б.В.Лавровым.</p>

                    <p>Работая над словарными статьями и участвуя в их обсуждении, Б.А.Ларин пришел к
                    заключению, что в случаях отсутствия слова в русских источниках "большую помощь
                    могут
                    оказать зарубежные памятники по русскому языку (словари, разговорники, описания
                    России... Эти источники имеют то преимущество, - напишет он позднее, в 1962 г.,- что
                    их
                    составители не были связаны традициями и нормами литературного русского языка, а
                    кроме того, их, по преимуществу купцов, интересовали слова обиходные, связанные с
                    бытом, торговлей и производством, а не с религией и философией. Ценно то, что если
                    все
                    наши русские источники дают слова в традиционном написании, то иностранцы
                    записывают их в своей, часто удачно приспособленной орфографии. Это сразу
                    раскрывает
                    особенности произношения данного слова" (Ларин Б.А. История русского языка и
                    общее
                    языкознание. М. 1977.- С.16-17). Тогда, в 1934 г., он послал в Англию письмо с просьбой
                    выслать фотокопию словаря-дневника Ричарда Джемса, а переизданная им в 1937 г. с
                    переводом, вступительной статьей и примечаниями "Русская грамматика"
                    Г.В.Лудольфа
                        (вышедшая в Оксфорде в 1696 г.) стала одним из источников Картотеки ДРС.</p>

                    <p>Б.А.Ларин, будучи единственным штатным сотрудником Группы ДРС, имел массу
                    обязанностей - организационных и научных. Он планировал работу Группы, размечал
                    памятники для выборщиков, инструктировал их и раздавал работу, проверял
                    выполненную
                    выборку и расстановку карточек в картотеке. Он комплектовал подсобную библиотеку
                    Группы, пополнял каталоги, составлял сметы расходов. Принимая работу, он составлял
                    счета на оплату для бухгалтерии, заносил в Тетрадь поступлений принятые материалы
                    и
                    выплаченную за них сумму. Он регулярно посылал отчеты о работе Группы ДРС в
                    Москву
                        академику М.Н.Сперанскому.</p>

                    <p>В Дневнике 15 мая 1934 г. он делает такую запись: "Написал отчет ак. Сперанскому". А
                    10
                    июля пишет: "Выполнены все просьбы ак. Сперанского (приготовлена ему и
                    И.А.Виноградову бумага, составл[ен] список источн[иков] Петр[овской] эпохи с копией,
                    сделана выписка из протокола о выборке собственных имен), сообщены все последние
                    сведения о ходе работ". Сам Ларин расписывал для КДРС некоторые рукописи
                        Румянцевского музея в Москве.</p>

                    <p>На 1 декабря 1935 г. в фонде Картотеки ДРС насчитывалось 371673 карточки.</p>

                    <p>Весной 1936 г. произошел переезд ее в другое здание, о чем в Дневнике сделана
                    запись:
                    "14-17 мая. Перевозка в новое помещение с Вас[ильевского] остр[ова] на Hабер[ежную]
                    9
                    января д. 18... 18-20 мая. Укладку материалов, наблюдение за перевозкой, раскладку
                    и
                    восстановление кабинета ДРС осуществили Б.В.Лавров, Е.М.Иссерлин, Р.С.Лисовская,
                    Е.П.Динариева, Н.М.Лысс, А.П.Конусов, Л.В.Успенский. Остальные по разным
                    причинам
                    принять участия в "переселении" и оказать своей помощи этому трудному делу не
                        могли".</p>

                    <p>Список слов, нуждающихся в наглядном пояснении иллюстрациями, А.П.Евгеньева
                    довела
                    до середины алфавита. Авторы словаря отдавали себе отчет в том, насколько труден
                    подбор иллюстраций (рисунков, символических изображений) к словам и реально
                    существующим вещам (булгак, рундук, ставец и др.), религиозным представлениям,
                    наименованиям мифологических животных и т.п. Брали за образец Reallexikon der
                    Vorgeschichte (Hrsg.v. Max.Ebert. Bd.I-XV.1924-1932), итальянский словарно-
                    этнографический
                    атлас, журнал "Worter und Sachen". Предполагали дать сводное изображение не только
                    реалий одного рода (шапок, повозок), но и древнерусских графических символов
                    (бортных
                    знамен, крюкового музыкального письма), показать особенности различных почерков -
                    устава, полуустава, вязи, скорописи (Проект, с.21-22). Эти трудности так и не были
                    преодолены: в пробных наборах "Словаря древнерусского языка XI-XVIII вв.(Л., 1941) и
                    "Исторического словаря русского языка XI-XVIII вв. (Л.-М., 1946) иллюстраций нет и о
                    них
                    не упоминается. Дать представление о древнерусских почерках удалось в издании
                    "Словаря русского языка XI-XVII вв.", в котором на форзацах помещаются отрывки из
                        различных текстов.</p>

                    <p>Коллектив ДРС в 1936-1937 гг. продолжал свою работу над словарем: готовил к
                    изданию
                    первый сборник своих трудов "История слов в русском языке", куда должны были войти
                    исследования о словах 1 бой, воровство, 1двор, дело, ино, лов, рухлядь 0 и др. Но
                    Группу
                        ожидали новые испытания.</p>

                    <p>В апреле 1937 г. на три четверти сократилось ассигнование средств на Словарный
                    отдел,
                    где шла работа как над Словарем древнерусского языка XV-XVII вв., так и над
                    Словарем
                    современного русского языка. С 10 апреля прекратилась выплата за сдельную работу.
                    В
                    приказе директора ИЯМ академика И.И.Мещанинова N 384 от 28 мая говорилось: "По
                    причинам сокращения объема финансирования работ по ДРС и Словарю русского
                    языка
                    предлагаю зав. Отделами тт. Державину Н.С. и Ларину Б.А. освободить и в
                    дальнейшем
                    не
                    привлекать к работе без разрешения Дирекции в качестве выборщиков и раскладчиков
                    следующих лиц с 1 июня 1937 г. ... по Древнерусскому [словарю]: Майков В.В., Акимов
                    И.В.
                    [так; нужно П.В.], Горунович Я.Я., Успенский Л.В.,Громова А.И. [так, нужно А.П.],
                    Моложавская Л.С., Бахмутова Е.И. [так; нужно Е.К.], Бабкин А.И. [так; нужно А.М.],
                    Богословский А.К., Лысс Н.М." Отстранялись от работы и "договорники Словаря
                    русского
                    языка: Виноградов Г.С., Ивойлов В.Н., Филиппов Н.Н., Порфирьев Н.Ф. [так; нужно
                    Н.А.],
                        Геккер С.Ф., Попова И.А., Холынский А.Г., Зеленин [без инициалов]".</p>

                    <p>В приказе от 7 июня 1937 г. N 387, посвященном специально Отделу ДРС, говорилось:
                    "Исключить из списков работников Института и в дальнейшем не привлекать к работе
                    без
                    особого в каждом отдельном случае разрешения Дирекции следующих гр-н [так!]:
                    Лавров
                    Б.В., Иссерлин [здесь и далее - без инициалов], Евгеньева, Котович, Приселков,
                    Смирнова,
                        Ульянова, Конусов, Абрамович, Лисовская, Кутилова.</p>

                    <p>В Отчете за 1937 г. Б.А.Ларин писал, что он обращался к директору ИЯМ с заявлением
                    "о
                    необходимости полного пересмотра плана и представил новый план в соответствии с
                    определившимися в тот момент условиями работы". Далее он сообщал: "На мне лежало
                    руководство Секцией (председателя мы не имеем уже 2 года) и вся организационная,
                    контрольная, инструкционная работа, а кроме того, все делопроизводство и
                    отчетность
                    Секции (в Секции не было технического секретаря при 22 сотрудниках в 1937 г.), а
                    также
                    заведование библиотекой и картотекой, наконец еще и хозяйственные заботы. Мною
                    проводилось обучение новых и молодых сотрудников. За январь-май 1937 г. состоялось
                    16
                    научно-производственных заседаний (3 из них по просьбе работников Ин-та истории АН
                    для
                    обсуждения трудных мест "Русской Правды")". В это же время он участвовал в
                    экспедициях, консультировал сотрудников, давал отзывы, выступал с докладами. Под
                    его
                    руководством вели научные исследования А.П.Конусов, А.П.Евгеньева, Е.М.Иссерлин,
                    Б.В.Лавров, А.Н.Котович, Н.А.Янко-Триницкая и др. И в то же время пополнялась
                        картотека: на 1 июля 1937 г. в фонде было 664 468 карточек.</p>

                    <p>Почти полгода Б.А.Ларин ходил в Картотеку ДРС один. Только 26 ноября в Дневнике
                    появилась запись прежней сотрудницы Н.А.Ульяновой с указанием времени ее работы
                    в
                    картотеке, а 1 декабря - А.С.Смирновой, которая против своей фамилии поставила
                    букву
                    "С" и "5 ящ." Это означало, что она занималась раскладкой материала на 1С 0 в пяти
                    ящиках. Развернутых записей, так характерных для 1934 г., не стало. (Подробнее об
                    этих
                    годах работы сотрудников Группы ДРС можно прочитать в ст. Астахиной Л.Ю. "Борис
                        Александрович Ларин" // Вопросы теории и истории языка. СПб. 1993).</p>

                    <p>В следующем 1938 г. Б.А.Ларин дважды делал доклады на сессиях Отделения
                    общественных наук АН. На протяжении всего года несколько раз менялось основное
                    задание для Группы ДРС. "Сначала работали над подготовкой Древнерусского словаря
                    XV-
                    XVIII вв.,- писал Б.А.Ларин, - затем предложено было переиздавать "Материалы для
                    древнерусского словаря ак. Срезневского, и только в конце года сложилось
                    окончательное решение, подтвержденное Президиумом АН, о составлении в первую
                    очередь Малого исторического словаря русского языка объемом около 200 печ[атных]
                    лист[ов]. К написанию хотя бы первых листов этого МИСРЯ нельзя было приступить до
                    конца года, так как в нем должен быть отражен материал за весь XVIII век, которого
                    еще
                    нет в картотеке". Кроме того, не сделано было еще систематизированного по векам и
                    жанрам каталога всех использованных источников. "Эта задача не м[ожет] б[ыть]
                    осуществлена, пока в составе штатных работников ДРС нет авторитетного
                    специалиста
                    по
                    истории древнерусской литературы, каким мог бы быть, например, чл.-корр.
                    Д.И.Абрамович, уже давно оказывавший нам некоторую помощь со стороны,- писал
                    Б.А.Ларин в Отчете за 1938 г., датированном 8 февраля 1939 г. - Только в конце
                    октября
                    1938 г. было предоставлено наконец помещение, небольшой кадр штатных [так в
                    Отчете
                    -
                    Л.А.] работников (3 новых редактора и 2 раскладчика). Привлечено было 29
                    выборщиков. С
                    ноября 1938 г. работа пошла хорошо и появилась твердая уверенность в возможности
                    выполнить ответственное задание по составлению Малого исторического словаря
                        русского языка".</p>

                    <p>Далее приводятся объемы раскладки и редактирования на буквы 1В (Чернявская С.Л.,
                    Иссерлин Е.М.), 1Г 0 (Немировская К.А.), 1Д 0 (Котович А.Н.), 1Л 0 (Конусов А.П.). "В
                    ноябре-декабре, - следует далее в Отчете,- приведены в полный порядок, проверены и
                    дополнены четыре каталога использованных источников (сотрудником А.П.Конусовым).
                    Составлен доп[олнительный] список использованных источников XVIII в. (163
                    названия).
                    Приведена в порядок с пополнением рядом необходимых изданий подручная
                    библиотека
                    ДРС (А.П.Конусовым). В ноябре-декабре состоялось 7 производственных совещаний
                    Отдела ДРС. Весь коллектив ДРС заключил договор о социалистическом соревновании
                    с
                    Отделом современного словаря, причем все сотрудники взяли на себя индивидуальные
                        обязательства сверх плана".</p>

                    <p>Приказом от 23 октября 1938 г. Б.А.Ларин назначался руководителем Отдела и
                    ответственным редактором по подготовке к изданию словаря с освобождением от
                    должности ученого секретаря ДРС, а 1 января 1939 г. он был утвержден в должности
                    руководителя Кабинета ДРС. На С.Л.Чернявскую приказано было "возложить
                    обязанности
                    секретаря по научно-технической части с 1 ноября". Внештатно были приняты
                    А.Н.Котович
                    (для проверки и раскладки буквы 1Л 0), А.П.Конусов (для проверки каталогов) и
                    М.Д.Приселков (для проверки материалов на букву 1Ж 0). Тоже внештатно на
                    должность
                    раскладчиков и выборщиков зачислялись Н.А.Ульянова и А.С.Смирнова, а на сдельную
                    оплату с выборкой на дому оформлялись П.В.Акимов, П.В.Булычев, А.П.Евгеньева
                    (теперь 1 - аспирантка ЛГПИ), Б.Л.Богородский (ассистент ЛГПИ), историк Б.А.Романов,
                    А.А.Скворцова, С.Ф.Геккер (для завершения работы над библиографическим
                    указателем
                    "Русский язык"), Б.И.Коплан, М.И.Белов, студенты В.А.Николаева, А.В.Адамович,
                    В.Т.Пашуто. Приказом от 20 ноября приняты на тех же условиях еще 5 человек:
                    Н.М.Соколова, Б.А.Цинман и студенты Л.А.Булюбаш, М.В.Артюшенков, М.Г.Овсова.
                    Впоследствии Б.А.Ларин напишет: "Много времени отняло у меня обучение студентов-
                    выборщиков, в 1-м квартале работало у нас 29 студентов. Часть отсеялась, но мне
                    удалось
                    приготовить ок[оло] 20 прекрасных выборщиков из студентов Ин-та им. Герцена и
                        университета".</p>

                    <p>Действительно, с 1 ноября 1938 г. в качестве внештатных выборщиков со сдельной
                    оплатой и выборкой на дому были приняты студенты ЛГПИ Сидоров А.С., Лучин П.Н.,
                    Матвеев Ф.М., Сорокин А.Г., Никулина Е.А., Капелюш Б.Н., Окунева Ю.Г., Якубова
                    Х.Ш.,
                    Ходова К.И., Ерофеева М.П., Цион А.Р., Вознесенская Д.А., Глускина С.М., Юпашевская
                    Е.М., Силина Н.Г., Орехов В.И. Приказом от 23 октября 1938 г. были установлены новые
                    расценки за выписки из источников разного рода. За одну карточку, выписанную из
                    изданных произведений XVII-XVIII вв., платили 25 коп., из печатных переизданий
                    произведений XI-XVII вв. - 30 коп., из первопечатных книг XVII и XVIII вв., находящихся
                    на
                    особом хранении в библиотеках, - 35 коп., за выписки из рукописных текстов XI-XVIII
                    вв.
                    -
                    50 коп., из неразборчиво написанных, смытых и скорописных текстов - 60 коп. За
                    разрезку
                    и расклейку по предварительно размеченным текстам полагалось по 10 коп. за
                    карточку.
                    За выписки двуязычные, т.е. с греческими, латинскими, польскими и др. параллелями,
                    платили по 60 коп. за карточку; за составление полного словоуказателя к тексту (чаще
                    -
                        изданному) - по 50 коп. за карточку.</p>

