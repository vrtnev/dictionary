                <h2 id="part3">Часть 3</h2>
                <div>
                    <p>На 1 января 1939 г. было собрано в КДРС 763 612 карточек. К составлению Краткого
                    (Малого) исторического словаря русского языка коллектив приступил только 1 марта
                    1939 г. За первое полугодие было написано 26 авторских листов. Но возникли
                    непредвиденные трудности. Из-за непредумышленного брака в работе пришлось
                    считать в одном авторском листе не 600, а 800 карточек, поэтому написанные к концу
                    1939 года 93 авторских листа "превратились" в 74. В Отчете за 1939 г. говорится:
                    "Качество работы редакторов [они составляли словарные статьи - Л.А.] нельзя признать
                    безукоризненным. Причины этого следующие: обрабатываются материалы, не
                    обследованные и не подготовленные не только лексикологически, но даже
                    филологически. Большинство текстов XVI, XVII, XVIII вв. не изучены во всех отношениях
                        и поэтому во многих своих частях представляют задачу чисто исследовательскую.</p>

                    <p>Вторая трудность в том, что не имея предшествующего большого исторического
                    словаря сразу делать краткий словарь, притом учебно-справочного назначения [так в
                    Отчете - Л.А.]. Это обязывает и к очень тщательному отбору материала и к длительной
                    повторной обработке семантических определений, наконец, к трудному переходу от
                    подробной полной классификации значений и употреблений к стержневой схеме
                    важнейших значений. Писать большой исторический словарь было бы во многих
                        отношениях легче, но для него понадобится втрое более богатая картотека".</p>

                    <p>Отмечались и положительные моменты работы в Группе ДРС: "Качество выборки очень
                    высокое, особенно в последнем квартале, когда работали только квалифицированные
                    выборщики... Раскладка новых поступлений протекала нормально. Все, что было
                    передано в Картотеку для обработки, поступило к редакторам. Кроме обычной
                    раскладки А.С.Смирнова производила специальную подготовку ящиков для очередной
                    работы редакторов, что облегчало их работу". Много чисто технической работы было и у
                    Б.А.Ларина: "Основную массу выборок приходится до сих пор проверять мне (из 103 848
                    карт[очек] мною проверено ок[оло] 50 000). Лежит непроверенным грузом ок[оло] 300
                    000 карт[очек] и ок[оло] 24 тыс. проверили Е.М.Иссерлин, М.Д.Приселков,
                    С.Л.Чернявская (небольшую помощь по проверке оказали мне также А.Н.Котович,
                    К.А.Немировская, и А.П.Конусов)... Подготовка Инструкции Краткого исторического
                    словаря осуществлялась на производственных совещаниях (за полугодие состоялось
                    17 производственных совещаний), на которых было обсуждено 15 статей написанного
                        словаря" (Отчет за 1939 г.).</p>

                    <p>Справочный аппарат картотеки требовал постоянного внимания руководителя ДРС, что
                    отразилось и в Отчете за первое полугодие 1939 г.: "Каталоги источников до декабря
                    1938 г. вел я (их четыре, и это отнимало время), затем эту работу продолжал
                    А.П.Конусов (бесплатно), а последние два месяца 1939 г. - С.Ф.Геккер, которой было
                    поручено произвести полный пересмотр всех каталогов для пополнения их имеющимися
                    в литературе данными, по точной датировке каждого слова в каждом значении - эта
                    последняя работа имеет первостепенное значение, она чрезвычайно облегчает
                    дальнейшую работу редакторов". Уточним: это поручение было дано С.Ф.Геккер 23
                        апреля 1939 г.</p>

                    <p>При всей загруженности "словарными" и "картотечными" обязанностями сотрудники
                    вели научную работу: "Приселков М.Д. в июне 1939 г. блестяще защитил свою
                    докторскую диссертацию. Г.Л.Гейерманс заканчивает большую работу по подготовке
                    издания "Русской Правды" и "Истории Сибири" [Г.Ф.]Миллера. Е.А.Рыдзевская сдала в
                    печать работу по вопросу об устных преданиях как источнике древнейшей русской
                    летописи, а А.П.Конусов и Е.М.Иссерлин сдали в [сборник ИЯМ - Л.А.] "Язык и
                    мышление" две работы по исторической семантике", - значилось в Отчете за 1939 г.,
                    хранящемся ныне в Межкафедральном словарном кабинете им.Б.А.Ларина СПб
                        университета.</p>

                    <p>К концу 1939 г. в картотеке было 909 015 карточек. Сотрудники ДРС стремились
                    включить в словарь как можно более широкий пласт лексики, поэтому они стали
                    составлять указатели слов к отдельным опубликованным и рукописным памятникам.
                    Так, в 1939-м и последующие годы были составлены указатели к Домострою и Киево-
                    Печерскому Патерику (П.В.Акимов), к Слову о полку Игореве (Д.И.Абрамович),
                    Задонцине (Л.С.Моложавская)???, Горе-Злочастию (М.А.Соколова)???, Торговой книге
                    XVI в. (И.Н.Шмелева) и др. С учетом данных этих словоуказателей трудились над
                        заполнением лакун, проверкой и исправлением неполных и сомнительных карточек.</p>

                    <p>Работа над библиографическим указателем "Русский язык" также подходила к
                    завершению. Оставалось произвести классификацию материала по разделам книги
                    (согласно утвержденной схеме) и составить подробный тематический указатель. В эту
                    книгу должны были войти все работы по русскому языку, в частности по лексике, за
                    1880-1938 гг. Кроме того, решено было сдублировать всю эту библиографическую
                    картотеку и составить указатели - этимологический и географический. Отделение
                        поручало эту работу Б.А.Ларину и С.Ф.Геккер.</p>

                    <p>А.П.Конусов продолжал пополнять подсобную библиотеку ДРС: в нее было добавлено
                    200 памятников, переплетено 409 книг, упорядочена расстановка. Были созданы
                        специально для редакторов настольные комплекты справочников.</p>

                    <p>Кипучая деятельность сотрудников ДРС в конце 1938 - начале 1939 г., напряженные
                    планы, энтузиазм совсем не означали благоденствия и спокойной работы. 3 января
                    1939 г. помощником директора ИЯМ по хозяйственной части Н.Т.Ухиным был издан
                    приказ "о рвачах и лодырях". В нем предписывалось завести в Отделах журналы для
                    учета прихода и особенно ухода сотрудников с работы, предлагалось подавать рапорты
                    на опоздавших. Рабочий день устанавливался с 10 до 17 часов с перерывом на обед с 13
                    до 14. В Дневнике стали появляться записи вроде следующих: "10 января 1939 г.
                    Ульянова недораб[отала] 2 ч[аса]. Отработ[ала] за 10 янв. 14-15-16[-го] по 1 ч[асу]. Итого
                    3 часа", или: "Смирнова А.С. Отработка вечером. Была у врача, работала без перерыва
                    1/2 ч[аса]. За вт[орник] 1 ч[ас] недоработ[ала] - отработала 1 ч[ас] 14 января", 4 апреля.
                    Смирнова. Ходила на рентген в амбулаторию. Затрач[ен] 1 ч[ас]. Отработала 15 апреля"
                        и т.п.</p>

                    <p>***</p>
                    <p>Весной 1940 г. было принято и в июне подтверждено на общем собрании Отделения
                    решение о том, что "Словарь древнерусского языка, как издание ИЯМ, должен носить
                    не нарочито учебный характер, должен быть не "малым" словарем, как преддверие
                    имеющего в дальнейшем быть обработанным "большого" древнерусского словаря, а
                    должен явиться нормальным изданием словаря, со всей полнотой отражая лексическое
                    состояние русского языка за период с XI до второй половины XVIII в.",- так писал в
                    своей Записке в Отделение АН назначенный в 1940 г. председателем Главной редакции
                    С.П.Обнорский, избранный недавно действительным членом Академии наук. Он
                    обосновывал принципы создания словаря: "Словарь должен... обнять всю основную
                    лексическую наличность русского языка... не только древнейшие ("исконные") русские
                    слова, но и заимствованные, не только общеупотребительные, но и специальные слова
                    и выражения (из области ремесел, торговли, культуры, наук и искусств феодального
                    русского общества). Слова даются с указанием, в лаконичном виде, всей подходящей
                    канвы их значений; значения документируются точными цитатами, редкие и исторически
                    важные слова сопровождаются полным перечнем текстуальной их документации; при
                    заимствованных словах по возможности указываются источники заимствований, в
                    необходимых случаях при русских словах даются славянские к ним параллели, а также
                    отмечается социальная принадлежность для слов специального употребления или
                    дается жанрово-стилистическая характеристика известных категорий
                    словоупотребления. Общий объем словаря не должен превышать 12 томов по 80-100
                        печ[атных] л[истов] каждый" (Моск. отделение Архива РАН. Ф. 1618, оп.1, ед.хр.34).</p>

                    <p>Такое решение Отделения, конечно, не могло положительным образом не повлиять на
                    коллектив, и в целом 1940 г., по-видимому, прошел без особых потрясений. Считаясь с
                    тем, что хронологические рамки словаря изменились, необходимо было пополнять
                    Картотеку ДРС материалом из памятников, созданных или переведенных в XI-XV вв. А
                    для этого нужно было привлечь специалистов, начитанных в древнерусской литературе.
                    Вот и появились в Отделе ДРС такие выборщики, как Ф.И.Покровский, тот самый,
                    который еще в начале века объездил вместе с Н.К.Никольским 35 городов, отыскивая
                    в хранилищах лучшие списки русских произведений. Он расписывал Апокалипсис XIII в.,
                    Измарагд XIV в. (по списку XVI в.), Библию Геннадия 1499 г. с греческими и латинскими
                    параллелями, рукописный Требник ХVI в. и Кормчую Балашова также ХVI в., хранящиеся
                    в БАН. Появился и бывший профессор Петербургской духовной академии
                    В.М.Верюжский, который также расписывал Геннадиевскую Библию 1499 г. с
                    греческими и латинскими параллелями, Великие Минеи Четии (Патерик Скитский XIV в.)
                    с греческими параллелями,. дополнял слишком краткие цитаты "разрезной" картотеки,
                    сделанной по Хронике Иоанна Малалы. Однако в Тетради поступлений его имени
                    упомянуто не было: вероятно, сказывалась в его судьбе фронтальная чистка
                    академического аппарата 1929 г., когда в государственных учреждениях не оказалось
                    места профессору СПб духовной академии, хотя его знания очень пригодились в работе
                    по пополнению Картотеки ДРС. Продолжал работу и бывший преподаватель гимназии,
                    член Археографической комиссии с 1908 г. П.В.Акимов, и специалист по русской
                    скорописи XVI-XVII вв. Н.В.Тимофеев, также член Археографической комиссии,
                    вовлекший в работу со скорописными памятниками свою дочь З.Н.Тимофееву
                    (Савельеву), впоследствии - известного палеографа, издателя произведений
                        В.Н.Татищева.</p>

                    <p>В Отделе ДРС шла обычная работа: составление и обсуждение словарных статей,
                    перепечатка на машинке и вычитка машинописи словаря, выборка из памятников и
                    раскладка новых поступлений, зачисление и отчисление внештатных сотрудников,
                    утверждение в должности - штатных. 30 декабря 1940 г. председатель Главной редакции
                    С.П.Обнорский получил от Дирекции ИЯМ благодарность "за ударную работу по
                    редактированию ДРС, по рецензированию многочисленных работ по славистике, по
                    систематической помощи учительству консультациями по разработке вопросов
                    русского языка и активное участие в научных совещаниях, созываемых дирекцией". Но
                    несколько раньше, в мае 1940 г., благодарность была объявлена внештатному
                    сотруднику ДРС А.П.Георгиевскому (чтецу) и агитатору Б.И.Коплану за работу среди
                        раненых бойцов в госпитале: шла финская война.</p>

                    <p>В апреле уходит на полставки редактор старш. научн. сотрудник К.А.Немировская. В
                    июле 1940 г. получает месячный отпуск А.С.Смирнова по состоянию здоровья (по
                    справке из тубдиспансера) без сохранения содержания, а на ее место принимают
                    Е.М.Юпашевскую для раскладки, работавшую ранее внештатным выборщиком. В
                    сентябре 1940 г. переведен в аспирантуру ИЯМ на стипендию А.П.Конусов, который в
                    марте был на военных сборах. Зачислен исполняющим обязанности старш. научн.
                    сотрудника и редактором ДРС с нормой 1 а.л. в месяц Л.В.Успенский. Осенью на
                    аналогичные должности были приняты Б.Л.Богородский, Б.И.Коплан и А.Н.Котович. С 10
                    сентября перешел на "полистную оплату" М.Д.Приселков, назначенный деканом
                    исторического факультета ЛГУ. С 1 октября временно в штат был зачислен редактором
                        П.А.Садиков, работавший по трудовому соглашению.</p>

                    <p>Отчет за второй квартал 1940 г. сделал А.Н.Котович, замещавший заведующего
                    Б.А.Ларина:
                    "I. По редакторской работе: составлено на 1А 0- 1В всего 25 075 карт[очек], или 41,8
                        листа (считая по 600 карт[очек] в листе).</p>

                    <p>II. По выборке: сдано выборщиками карточек 39 365.</p>

                    <p>III. Производственных совещаний - 8 (обсуждение составленных для ДРС статей и
                    вопросы инструкционного характера, месячные отчеты и планы)". Такой вот
                    лаконичный
                        отчет. Видимо, работа входила в привычное русло.</p>

                    <p>И вот 2 января 1941 г. Б.А.Ларин, "издает", так сказать, шутливо-серьезный приказ,
                    сохранившийся в архиве С.Л.Чернявской. Ирония и нарочитая патетика говорят о
                    хорошем настрое, а приводимые факты точно отражают деятельность сотрудников
                        Отдела ДРС.</p>

                    <p>"За истекший 1940 г., ознаменовавшийся огромными достижениями по составлению
                    Большого Словаря древнерусского языка, выражаю благодарность следующим
                        стахановцам-лексикографам:</p>

                    <p>1. И.о. старш. науч. сотр. Котовичу А.Н. За выдающуюся полноту цитатного
                    материала,
                    богатство энциклопедических справок, смелые этимологии, непрестанную
                        консультацию, даваемую всем сотрудникам ДРС и многим прочим лицам.</p>

                    <p>2. И.о. старш. науч. сотр. Коплану Б.И. За безупречное ведение Главной редакции, что
                    привело к выражению благодарности Дирекцией ИЯМ ее председателю [т.е.
                    С.П.Обнорскому - Л.А.]. За рекордное количество написанных редакторских листов, за
                    написание сверх плана капитального исследования о Федоре Каржавине, Радищеве и
                        Крылове.</p>

                    <p>3. Мл. науч. сотр. Геккер С.Ф. За составление полного указателя источников ДРС, за
                    воспитание у редакторов библиографических навыков, за систематическое
                        перевыполнение редакторского плана.</p>

                    <p>4. Мл. науч. сотр. Чернявской С.Л. За бдительный и неустанный контроль над
                    выборкой;
                        за сверхплановое выполнение множества административных поручений.</p>

                    <p>5. Мл. науч. сотр. Гейермансу Г.Л. За разоблачение скрытых источников Срезневского,
                    за преодоление бешеного сопротивления редакторов требованию о раскрытии
                    серийных
                        обозначений, за героическую борьбу против завышения редакторских норм.</p>

                    <p>6. Мл. науч. сотр. Иссерлин Е.М. За успешное совмещение двоякого рода обязанностей:
                        редактора и диссертанта [она готовила кандидатскую диссертацию - Л.А.].</p>

                    <p>6а. И.о. старш. науч. сотр. Богородскому Б.Л. Пока не за что.</p>

                    <p>7. И.о. старш. науч. сотр. Успенскому Л.В. За бессмертную поэму "О словарном
                    послушничестве и о горечи разлуки с ДРС". За верное береженье места редактора, за
                        благие обеты.</p>

                    <p>8. Научно-технич. сотр. Смирновой А.С. За скорую помощь редакторскому коллективу
                    по
                    самым разнообразным разделам и заявкам, за мужественный отказ от ежемесячных
                        бюллетеней, чем так грешат другие работники картотеки.</p>

                    <p>9. Редактору-договорнику Садикову П.А. За непревзойденную трудоспособность, за
                    образец дружеской помощи своим соседям по работе, за трактаты о боге и грамоте,
                        имеющие историческое значение.</p>

                    <p>10. Непоименованным здесь лицам благодарность будет выражена в следующем году,
                        буде не окажутся в нетех.</p>

                    <p>11. О денежном вознаграждении оставьте суетные помышления. Довольно с вас и
                        тортов, купленных на собственные деньги.</p>

                    <p>12. Приказ объявить во всех столах, всех словарях и курилках. Зав. Отделом ДРС
                        [Б.А.Ларин]. Верно: С.Чернявс[кая]".</p>

                    <p>Успехи Отдела в работе над Большим словарем древнерусского языка были
                    несомненны: 2 июня 1941 г. был подписан в печать пробный набор "Древнерусского
                    словаря XI-XVIII вв." В начале этого выпуска дано обращение С.П.Обнорского и
                    Б.А.Ларина к читателям с просьбой сообщить, какой из трех предлагаемых "вариантов
                    был бы наиболее удачным образцом набора нашего словаря. Если... будут какие-нибудь
                    дополнительные указания и советы по оформлению этого издания, мы будем... очень
                    признательны за своевременное их сообщение". Обращение датировано 31 мая 1941 г.
                    Давая в выпуске три вида набора словарного текста и издавая этот выпуск тиражом в
                    80 экземпляров, авторы хотели услышать мнение будущих читателей-специалистов о
                    способах выделения заголовочных слов, о размещении компонентов словарной статьи,
                    о количестве строк на странице (т.е. плотности текста), о способах выделения и месте
                    словосочетаний и вариантов, грамматических помет и т.п. Но узнать это мнение им не
                    пришлось: 22 июня 1941 г. началась война с гитлеровской Германией, что в корне
                        нарушило налаживающуюся было работу.</p>

                    <p>В Отчете за июль-сентябрь 1941 г. сообщается: "Начиная с июля м[еся]ца нормальная
                    работа была нарушена по следующим основным причинам: 1. Начались оборонно-
                    строительные работы по гражданской трудовой повинности, в которых должны были
                    участвовать в обязательном порядке в разные сроки 6 (шесть) сотрудников ДРС.
                    Богородский работал по трудповинности 57 дней, Коплан - 36 дней, Гейерманс - 24 дня,
                        Чернявская и Геккер - по 14 дней.</p>

                    <p>2. Прошли сокращения штатов ИЯМ, в силу чего были отчислены с середины августа
                    штатные сотрудники ДРС: Б.А.Цинман, С.Л.Чернявская, А.П.Евгеньева, А.С.Смирнова и
                    С.Ф.Геккер. В первые же дни войны были отчислены договорники-редакторы
                    П.А.Садиков и Л.В.Успенский... Кроме того, все оставшиеся в штате сотрудники несли
                    через день-два обязательные дежурства по ПВО; два сотрудника (Гейерманс и Коплан)
                    с конца сентября начали ежедневно занятия по программе Всеобуча по рабочему отряду
                    АН, а Коплан кроме того прошел 40-часовые районные курсы ПВХО. Зав. ДРС Б.А.Ларин
                    около 1,5 мес. выполнял специальные оборонные работы в штабе действующей
                    Красной
                        Армии Северо-Западного фронта.</p>

                    <p>Таким образом план работы ДРС за 3-й квартал, рассчитанный на продукцию 11
                    редакторов, не мог быть выполнен по указанным основаниям силами 5-ти редакторов.
                    За июль-сентябрь по редакторской работе выполнено 12 авторских листов (5 500
                    словарных карточек) по II-му тому (начиная с буквы 1В 0). Выборки для Картотеки ДРС
                    не продолжались ввиду полного исчерпания ассигнований в первом полугодии. Работа
                    Главной редакции прекратилась в связи с отъездом председателя Главной редакции
                        С.П.Обнорского".</p>

                    <p>И все-таки Б.А. Ларин, оставаясь в Ленинграде, не опускал руки. В 4-м квартале 1941 г.
                    по плану предполагалось отредактировать "10 авторских листов II тома ДРС (буква
                    1В 0) из расчета около 1 листа на каждого из 3-х оставшихся в штате редакторов (1
                    редактор И.И.Матвеев уволен по сокращению штатов с 8 октября, 1 редактор
                    Б.Л.Богородский переведен тогда же на половинный оклад с половинным рабочим
                    днем)". Предполагалось к концу года возобновление работы Главной редакции,
                    перепечатка на машинке II тома ДРС и подготовка его к печати. "Выполнение
                    намеченного плана, - писал далее Б.А.Ларин, - будет зависеть от того, не будут ли
                    сотрудники ДРС посылаемы на оборонные или иные какие специальные работы, а
                    также насколько будут обеспечены для наличных сотрудников ДРС терпимые
                        физические условия работы".</p>

                    <p>19 января 1941 г. скончался М.Д.Приселков, в конце года - А.Н.Котович. В 1942 г. во
                    время блокады умер А.П.Конусов, не вернулся домой, выйдя на улицу, В.Н.Ивойлов. В
                    эвакуации в Яранске оказались Б.А.Ларин, А.П.Евгеньева и А.А.Скворцова, в Ташкенте
                    - С.П.Обнорский, С.Г.Бархударов и Б.Л.Богородский, в Казани и затем в Алма-Ате -
                    Е.Н.Шипова, в Красноярске - С.Л.Чернявская. Окончившие ЛГПИ в 1941 г. студенты,
                    бывшие выборщики ДРС, оказались на фронте 1 0или в эвакуации, Был призван в
                        армию, а затем арестован и погиб в декабре 1941 г. Б.И.Коплан.</p>

                    <p>Д.А.Вознесенская вспоминает об этом времени: "Сразу же, в конце июня 1941 г.,
                    вместе с группой студентов и преподавателей Герценовского института выехала на
                    оборонные работы под г. Лугу. Затем из-за продвижения немецких войск нас вернули в
                    Ленинград и мы копали проти- вотанковые рвы под Пулковом буквально под оьстрелом
                    из-за Пулковских высот. затем с группой студентов и аспирантов перешла на
                    казарменное положение в составе команды ПВО. тушили зажигалки (бомбы) на
                    чердаках, дежурили в госпиталях, разгружали каменный уголь из вагонов и т.д. Жи- ли
                    в
                    бомбоубежище под зданием института. В начале продолжали некоторые занятия. Так,
                    помню, Б.А.Ларин приходил к нам в бомбоубежище и вел за- нятие со своими
                    аспирантами. Приходить было опасно. Уже начались обс- трелы города..." (Письмо от
                    24
                        февр. 1993 г. Хранится в Архиве Карто- теки ДРС в ИРЯ РАН).</p>

                    <p>Сохранилось письмо Б.А.Ларина, адресованное С.Л.Чернявской из г. Яранска
                    Кировской обл., где он с семьей жил в эвакуации. В нем, несмотря на неисчислимые
                    беды и несчастья, перенесенные большой семьей в невероятно трудных условиях,
                    выражается уверенность в том, что "впереди еще немало хорошего. Мы вернемся в
                    любимый город,- пишет Б.А.Ларин, - к друзьям, к своей работе". Больше всего
                    тревожит
                    "неработоспособность, то, что моя докторская диссертация не продвинулась ни на йоту
                    с отъездом из Ленинграда, что здесь никаких нужных мне книг нету, что не с кем
                    советоваться - сам тут за старшего. Товарищеской среды нет. Одна А.П.Евг[еньева] да
                    еще - более отчужденная А.А.Скворцова. Они слишком еще маленькие, чтоб с ними я
                    чувствовал себя не одиноким. Часто переписываюсь с Львом Влад[имировичем]
                    Щербой. Вот чье присутствие здесь много бы украсило мою жизнь и успокаивало бы
                    меня. Он очень бодр и много пишет, неутомимо работает..." Письмо датировано 23
                        февр. 1943 г.</p>

                    <p>С 1 июля 1942 г. была восстановлена на работе остававшаяся в Ленинграде
                    С.Ф.Геккер. Она вносила поправки и уточнения в уже составленные словарные статьи,
                    особенно если это казалось датировки цитат, шифров источников и др., берегла и
                        пополняла картотеку: более 5000 карточек новых поступлений внесла она в ее фонд.</p>

                    <p>В конце 1944 г. Б.А.Ларин вернулся в Ленинград и приступил к работе. В 1945 г. были
                    зачислены старш. научн. сотрудниками Е.М.Иссерлин (с февраля) и Б.Л.Богородский (с
                    конца мая); с начала августа приступила к работе С.Л.Чернявская, а с сентября -
                        Б.А.Цинман, оба на должности младш. научн. сотрудников.</p>

                    <p>Тогда же, в 1944 г., был образован Институт русского языка, в который естественно
                    вошел Отдел ДРС. Директором ИРЯ стал академик С.П.Обнорский. В 1945 г. в план
                    Отдела входила работа над 30-35 большими выпусками (по 100 печатных листов
                    каждый)
                    "Исторического словаря русского языка XI-XVIII вв.", начало которому могли положить
                    составленные до войны материалы I и II томов. Теперь в I том вошел отрезок 1А 0 -
                    1Бегущий, 0 во II - 1Беда - Бяшенина 0, в III - 1В 0 - 1 Возглавица, в IV - 1Возглаголать -
                    Высокошествие 0 и так далее. В связи с этим заданием коллектив в составе всего 6
                    штатных сотрудников приступил к пересмотру объемов и к 1 0исправлению первого
                        тома.</p>

                    <p>Окончание Великой Отечественной войны в 1945 г. и возвращение к мирному труду
                    активизировало научную мысль сотрудников Отдела исторического словаря, которая
                    так долго не находила выхода. Б.А.Ларин пишет словарную статью о союзе " 1А 0".
                    Энергично готовит к изданию новые источники, не использовавшиеся ранее в русской
                    исторической лексикографии, "Парижский словарь московитов 1586 г." и "Дневник-
                    словарь Р.Джемса", пишет статьи, посвященные их фонетическому, морфологическому
                    и лексическому анализу, что впоследствии вошло в его докторскую диссертацию.
                    Однако издать "Парижский словарь московитов" ему удалось в 1948 г., а "Дневник
                    Р.Джемса" - только в 1959 г. Он участвует в диалектологических конференциях, в
                    подготовке Атласа украинских говоров, выступает с докладами и лекциями о
                    подготовке Диалектологического атласа русского языка, редактирует два тома Ученых
                    записок ЛГУ, один из которых был посвящен памяти Л.В.Щербы - учителя, которого он
                    потерял в конце 1944 г. У него - три аспирантки: С.И.Груздева - по литовскому языку,
                    Л.С.Ковтун и И.Н.Шмелева - по русскому, в 1948 г. к нему в аспирантуру поступила
                    В.П.Фелицына. Он помогает Е.М.Иссерлин в работе над кандидатской диссертацией
                    "Говоры Пензенской области", которая защитила ее в 1945 г. Он выступает оппонентом
                    на диспуте Г.М.Милейковской "О грамматической терминологии М.В.Ломоносова". И
                    конечно, отдает дань давнишнему своему увлечению: пишет статью "Об изучении в
                    переводах древнеиндийской поэтики" в Лингвистический сборник ЛГУ. В 1945 г.
                        Б.А.Ларин был избран членом-корреспондентом Академии наук Украины.</p>

                    <p>В 1946 г. в Секторе историческогого словаря правили и редактировали II том объемом
                    32 авторских листа. В этом же году вышел пробный выпуск "Исторического словаря
                    русского языка XI-XVIII вв." В нем, так же, как и в пробном выпуске 1941 г. в.,
                    выражалась просьба сообщить свои мнения относительно трех способов набора
                    словарных статей и давались образцы набора. Авторы были те же самые, что и в 1941
                    г., только фамилии пятерых из них были помещены в черные рамочки: так авторские
                        коллективы сообщают читателям о своих утратах.</p>

                    <p>Выборки из памятников делали Л.С.Ковтун и И.Н.Шмелева, проверяла их
                    С.Л.Чернявская, она же консультировала внештатных выборщиков. Одновременно она
                    готовила к изданию памятник XVI в. "Книга глаголемая Назиратель". Единственный
                    рукописный список этой книги, который находится в ГИМ, в Москве, она переписала
                    дважды. Для картотеки его расписывали Н.П.Попов и Б.А.Ларин еще в довоенные годы.
                    Сам Б.А.Ларин заканчивал работу над докторской диссертацией об иностранных
                    источниках по разговорному языку Московской Руси, писал этюды по истории слов для
                        сборника трудов Отдела исторического словаря.</p>

                    <p>На 1946-1952 гг. был принят пятилетний план Сектора. Намечалось издание памятников
                    как источников для словаря и историко-семасиологические исследования по русскому
                    языку. Кроме "Назирателя" планировалось издать тексты "Мастерских книг", над
                    которыми работал Б.А.Ларин, и Торговую книгу XVI в., которую изучала аспирантка
                    И.Н.Шмелева. В план вошла и редакторская подготовка 25 листов однотомного так
                    называемого "учебного" Словаря древнерусского языка Д.И.Абрамовича: в первом
                        полутоме должны были быть буквы 1А-О 0.</p>

                    <p>С целью охвата в словаре возможно большего объема лексики предполагалось
                    продолжать составление словоуказателей, например, к сочинениям Ивана Грозного,
                    протопопа Аввакума (два указателя), к "Сказанию Авраамия Палицына", к книге
                    Г.Котошихина "О России в царствование Алексея Михайловича", к Псковской судной
                    грамоте. В апреле 1947 г. Б.А.Ларин защитил докторскую диссертацию "Три
                        иностранных источника по истории русского языка XVI-XVIII вв."</p>

                    <p>В конце 1947 г. верхней границей Исторического словаря 1, 0 по решению Отделения
                    литературы и языка АН СССР, предлагалось считать 1725 г. Объем его теперь
                    сокращался до 12 томов по 100 печатных листов. В I томе теперь помещался отрезок
                    1А 0 - 1Благонько 0. Он по плану должен быть отредактирован и подготовлен к печати
                    в
                    декабре 1947 г., а II и III тома - в декабре 1948 г. И пришлось из I-го тома изымать
                    словарные статьи, включающие материалы, датированные более поздним временем.
                    Было снято более 100 словарных статей, многие были сокращены или объединены в
                    одну. В этой работе участвовали Е.М.Иссерлин (отредактировала 1 авт. лист),
                    С.Ф.Геккер (2 а.л.), Б.А.Цинман (2 а.л.), Л.С.Ковтун (4,5 а.л.) и сдавшая уже
                    кандидатский
                    минимум В.П.Фелицына (1 а.л.). Б.А.Ларин выступил с докладом "О принципах
                    разработки исторического словаря", прочел цикл лекций в Латвийском университете
                    (Рига) о том, как преломляется новое учение о языке [Н.Я.Марра] в современной
                    разработке русского языка, в ЛГУ - об исторических словарях русского языка. Отделы
                        были переименованы в сектора.</p>

                    <p>В 1948 г. опять встал вопрос об изменении хронологических рамок словаря. В Отчете за
                    1948 г. Б.А.Ларин писал: "Филин [Ф.П.] сообщил нам о постановлении Бюро Отделения
                    литературы и языка расширить хронологические рамки Исторического словаря в целях
                    полного смыкания со Словарем современного русского языка АН СССР, доведя
                    использование источников до 70-х годов XVIII в." Соответственно в Секторе отложили
                    текущую работу над II томом и стали дополнять готовую уже машинопись I тома,
                        "приостановив работу над ним издательства АН СССР".</p>

                    <p>Сохранился "Тематический план Сектора Исторического словаря на 1949 г."
                    Намечалась "подготовка к печати 2-й половины II тома / 1Благонько-Бяшенина/ 0 ок. 70
                    авт. л. после Главной редакции и сдача его в РИСО", затем "составление 2-й половины
                    III тома / 1Виться-Воск/ 0 ок. 50 авт. л." и "подготовка к печати после Главной редакции
                    1-й и 2-й половины III тома / 1В-Воск/ 0 ок. 100 авт. л." (Архив КДРС, папка 17, л. 102).
                    Теперь расширялись границы I тома: в него входили полностью буквы 1 А 0 и 1 Б 0. В
                    "Объяснительной записке к плану Сектора Исторического словаря на 1949 г.",
                    составленной одновременно с планом Сектора заведующим Б.А.Лариным, наряду с
                    просьбой о необходимости добавить в штат двух науч.-техн. сотрудников и
                    машинистку,
                    есть раздел "О составе I-го тома Словаря", в котором приводится Выписка из
                    Протокола заседания бюро РИСО от 23 июня 1948 г.: "I-й том Исторического Словаря
                    первоначально составлявший ок. 100 авт. л., был представлен в РИСО после ряда
                    сокращений в объеме 63 авт. л. ( 1А-Бегущий) 0, но по ходатайству Сектора
                    Исторического Словаря ИРЯ объем его был утвержден в 100 авт. л. /до слова 1
                    Благонько/ 0 и для пополнения его объема в I-й том была перенесена начальная часть
                        II-го тома / 1Беда-Благонько/ 0 ок. 40 авт. л." (Там же, л.105).</p>

                    <p>Переработанный по новому плану I том 1А 0 - 1Благонько 0 был отредактирован
                        Б.А.Лариным и в 1949 г. утвержден к печати Ученым Советом ИРЯ.</p>

                    <p>Ранее же в плане предусматривалось завершение редакторской работы над I томом в
                    1947 г., а над II и III -- в 1948 г., чему, естественно, помешали вышеизложенные
                    обстоятельства и решения. "Срыв плана, частые изменения направления и характера
                    работы, отсутствие твердой линии привели к тому, что Дирекция Института и Бюро
                    Отделения вынуждены были создать Комиссию по обследованию работы ДРС, -
                    говорится в Записке "Исторический словарь". - Комиссия в составе акад.
                    В.В.Виноградова, чл.-корр. В.П.Адриановой-Перетц, чл.-корр. Д.И.Абрамовича, Ученого
                    секретаря ИРЯ Е.А.Комшиловой и секретаря Партбюро Института В.А.Басиева признала
                    состояние работы в Группе ДРС неблагополучным" (Архив КДРС. Папка N 25, л. 165.
                        Машинопись с правкой А.П.Евгеньевой).</p>

                    <p>На основании "Итогов проверки работы Института за 1948 г." и Докладной записки
                    зам. директора Ин-та проф. Ф.П.Филина был издан приказ директора ИРЯ акад.
                    С.П.Обнорского, в котором говорилось: "Старшего научного сотрудника ЛО ИРЯ,
                    исполняющего обязанности заведующего Сектором Исторического словаря, проф.
                    Б.А.Ларина за невыполнение плана Сектора освободить от работы в Секторе 1 апр.
                    1949
                    г. и перевести на должность старш. науч. сотрудника в Диалектологический сектор"
                    (Московское отд. Архива РАН, ф. 678, оп.2, ед. хр. 54). А другим приказом акад.
                    С.П.Обнорского предписывалось "проф. Б.А.Ларина с 16/IX освободить от работы в
                    Институте за систематическое невыполнение плана по сектору Исторического словаря
                    русского языка и в последние месяцы - по Диалектологическому сектору. Основание:
                    Докладная записка зам. директора проф. Ф.П.Филина" (Там же; и ф.672, оп.4, ед.хр.
                        135, л.52).</p>

                    <p>За "невыполнение плана" Б.А.Ларину был вынесен выговор. А 1 апреля 1949 г. он был
                        освобожден от заведования Сектором Исторического словаря.</p>

                    <p>На его место был назначен С.Г.Бархударов, зам. директора Ленинградского отделения
                    ИРЯ, которому были переданы и обязанности редактора I тома. Теперь временные
                    границы словаря простирались от XI в. до 1700 г. вместо прежних - до 70-х годов XVIII
                    в.
                    Объем словаря устанавливался в 7-8 томов по 100 авторских листов в томе, срок - 10
                    лет. Теперь редакторы-составители должны были составлять по 30-33 авторских листа
                    в полугодие. Так как изменялись принципы Словаря, предписывалось соответственно
                    изменить и инструкцию. Ее переработал С.Г.Бархударов, в корне изменив концепцию
                    словаря. В апреле 1950 г. на совещании в Секторе обсуждали новую инструкцию.
                    Старш.
                    научн. сотрудник Е.М.Иссерлин, с 1934 г. работавшая в КДРС, являвшаяся много лет
                    редактором словаря, и принятая в Сектор после успешной защиты диссертации
                    Л.С.Ковтун возражали против внесения коренных изменений в готовый первый том,
                    против нового изменения плана словаря. Они считали, что словарь должен выйти
                    таким,
                        как его отредактировал Б.А.Ларин.</p>

                    <p>В июле 1950 г. появилась книга И.В.Сталина "Марксизм и вопросы языкознания", и
                    сотрудникам было предложено вновь пересмотреть готовые материалы с учетом
                        положений этого труда.</p>

                    <p>В июле 1950 г. был ликвидирован Институт русского языка на основании постановления
                    Президиума АН СССР (Протокол N 17, параграф 371 от 26.7.1950), а его Словарный
                    сектор переведен в Институт языкознания (ИЯ). С 1 янв. 1951 г. заместителем
                    заведующего Словарным сектором по Группе Словаря древнерусского языка была
                        назначена А.П.Евгеньева.</p>

                    <p>Весь 1951 год сотрудники ДРС С.Г.Бархударов, А.П.Евгеньева, С.Ф.Геккер,
                    И.И.Матвеев и И.Н.Шмелева перерабатывали и редактировали I том. Теперь в него
                    должен был войти словарный отрезок 1А 0- 1Воз 0, что в прежнем словаре составляло
                    почти три тома. Материал на букву 1 А отредактировал С.Г.Бархударов, 1Б-Блазньба 0
                    -
                    И.И.Матвеев, 1Блакитный-Воз 0 - А.П.Евгеньева. Редактирование этого нового тома
                    было закончено весной 1952 г. 9 апреля он обсуждался на расширенном заседании
                    Словарного сектора ИЯ и Главной редакции словаря в Ленинграде, а 18 апреля - на
                        заседании Русской секции Ученого Совета ИЯ в Москве.</p>

                    <p>В Архиве КДРС сохранилась "Стенограмма заседания Русской секции Ученого Совета
                    Института языкознания АН СССР, посвященного обсуждению первого тома Словаря
                        древнерусского языка".</p>

                    <p>Во вступительном слове А.П.Евгеньева подчеркнула необходимость и важность такого
                    словаря, указала на те задачи, которые ставили перед собою сотрудники,
                    перерабатывая и редактируя I том. Остановившись на хронологических границах (XI в.
                    -
                    1700 г.), она определила это время как рукописный период жизни письменного языка.
                    Что касается важнейшего вопроса определений, то, по ее словам, редакторы
                    "старались подойти с точки зрения специфических особенностей древнерусского языка
                    в его отличии с одной стороны, и с другой стороны - в его связях с современным
                        языком, в его отношениях с ним" (Стенограмма, с.273).</p>

                    <p>А.П.Евгеньева уделила внимание истории создания словаря, начиная с 1925 г.
                    Упомянув о многократных изменениях плана словаря, она подвергла критике прежние
                    принципы Древнерусского словаря, разработанные Б.А.Лариным в его Проекте: "Самой
                    яркой особенностью древнерусского Словаря этого периода,- говорится в
                    Стенограмме,- были не только марристские позиции, но и отказ от предшествующей
                    русской лексикографической традиции, признавались лишь некоторые
                    западноевропейские словари реалий в качестве образца, с которыми нужно считаться"
                    (с.277). Говоря о кадровом составе Группы ДРС в 1934-1940 гг., А.П.Евгеньева
                    отметила, что "кроме Ларина в Словаре не было ни одного опытного и знающего
                    филолога. Организуя работу, Б.А.Ларин пригласил с одной стороны историков, а с
                    другой - молодежь, у которой не было достаточных филологических, ни тем более
                    лексикографических знаний. Я об этом могу говорить с полным правом и основанием,
                        так как сама начинала в это время что-то делать.</p>

                    <p>Совершенно естественно, что только при таком составе сотрудников не искушенных
                    совершенно в лексикографии, но воодушевленных громкими революционными
                    фразами, руководителю можно было подготовить идею синтеза узко-филологического
                    словаря с историко-терминологическим и со словарем культурных реалий, объявить это
                    "подлинно научным историческим Словарем", который должен служить для вскрытия
                    классовых идеологий и быть "новым этапом в области исторической лексикографии".
                    Полное отрицание предшествующей традиции и связанное с этим отсутствие
                    лексикографической грамотности наложило глубокий отпечаток на всю работу
                    сотрудников и с предельной яркостью сказалось и на представляемом сейчас томе"
                    (с.278-279). Приоткрывая завесу над взаимоотношениями в руководстве коллективом
                    Древнерусского словаря, А.П.Евгеньева продолжала: "То разумное начало, которое
                    стремились внести С.П.Обнорский и С.Г.Бархударов, когда они были введены в состав
                    Главной редакции в 1940 г., отвергалось и отрицалось. Отношения с Главной
                    редакцией
                    дошли до того, что Главной редакции материал на просмотр или совсем не давался или
                        давался в черновиках, до редактирования их Лариным.</p>

                    <p>После освобождения Б.А.Ларина руководство Словарем было поручено
                    С.Г.Бархударову,
                    который начал с переработки инструкции. Предложенный им проект инструкции
                    встретил настолько резко отрицательную критику со стороны представителей, если
                    можно так сказать, ларинско-марровской группы, что Степану Григорьевичу пришлось
                        внести туда целый ряд исправлений" (с.279).</p>

                    <p>Признавая, что в основу представленного к обсуждению тома 1А-Воз были положены
                    составленные Группой Б.А.Ларина материалы, 1 0А.П.Евгеньева говорила о том, что в
                    Словаре "было очень много лишнего. При работе над материалами обнаружилось и
                    другое: количество подготовленного за 15 лет материала оказалось настолько
                    незначительным, что оно почти полностью вместилось в первый том" (с.299). Приведем
                    еще одно ее замечание: "Ознакомление со считавшимися готовыми и
                    отредактированными Б.А.Лариным материалами ( 1А 0- 1Благонько 0), а также с
                    подготовленными сотрудниками, но не отредактированными материалами показало, что
                    следовало бы начать составление Словаря заново. Но задолженность Института по
                    Словарю древнерусского языка была настолько чудовищной, что об этом не могло быть
                    и речи" (там же). Возможно, это было бы единственно правильным решением в то
                    время: когда изменилась основная концепция словаря, переработана инструкция, то из
                        прежних материалов могли быть использованы, по-видимому, лишь цитаты памятников.</p>

                    <p>С критическими замечаниями выступили В.В.Виноградов, П.Я.Черных, А.Н.Робинсон,
                    В.Н.Сидоров, А.С.Львов, И.А.Оссовецкий и С.И.Ожегов. Решением этого заседания
                    было рекомендовано исправить недочеты, дать прочитать этот том одному опытному
                        редактору и опубликовать.</p>

                    <p>В это же время продолжалась редакторская работа над II томом, к которой кроме
                        прежних сотрудников подключились Л.Л.Кутина, Е.В.Матвеева и В.Л.Георгиева.</p>

                    <p>Однако после обсуждения состояния словарной работы в ИЯ 4-го июля 1952 г.
                    Президиум АН СССР принял постановление N 391 "О работе Словарного сектора
                    Института языкознания АН СССР", в котором отмечалось, что "Словарный сектор ИЯ
                    АН СССР (заведующий - член-корреспондент АН СССР С.Г.Бархударов) не выполнил
                    поставленных перед ним задач и не стал до сих пор руководящим центром словарной
                    работы, ведущейся в академиях союзных республик и филиалах АН СССР". Далее
                    говорилось, что "особенно пострадал от вредного влияния т.н. "нового учения" о языке
                    Словарь древнерусского языка". Был подготовлен только один том, "в Секторе слабо
                    велась теоретическая работа. Не были приняты меры по подготовке
                    квалифицированных лексикографических кадров". Поэтому было принято решение
                    "временно приостановить работу над Словарем древнерусского языка, сняв его с плана
                    1952 года" (Изв. ОЛЯ. 1952. Т.XI. В.6. - С. 572). Такое решение, видимо, было
                        неожиданностью и для А.П.Евгеньевой.</p>

                    <p>Считая первоочередной задачу выпуска словарей современного русского языка,
                    Президиум отпускал дополнительные средства, выделял новые штатные единицы.
                    Перед сектором Словаря современного русского языка, "по требованию широкой
                    общественности", как было сказано в Постановлении, выдвигалась задача создать
                    Словарь русского языка в 3-х томах. Была сформирована Редакционная коллегия под
                    председательством чл.-корр. АН СССР С.Г.Бархударова, в которую вошли чл.-корр. АН
                    СССР М.П.Алексеев, канд. филол. наук Г.П.Блок, акад. В.В.Виноградов, докт. филолог.
                    наук А.П.Евгеньева (Ученый секретарь), акад. С.П.Обнорский, канд. филол. наук
                    Ю.С.Сорокин. Сотрудники ДРС вошли в группу "нового словаря". Редактором I тома
                    была А.П.Евгеньева, помощником редактора - С.Ф.Геккер. Коллектив занимался
                    составлением в 1952-1960 гг.; издавался "Словарь русского языка" в 4-х томах в 1957-
                        1961 гг. Работа над Древнерусским словарем прекратилась.</p>

                    <p>В Картотеке ДРС было около 1,2 млн карточек.</p>

                    <p>В сентябре 1952 г. Картотека ДРС была перевезена в Москву. В составе вывезенных
                    материалов были и новые неразложенные поступления, и ящики с карточками,
                    изъятыми из рукописи Словаря при редактировании его в 1951-1952 гг. Карточки со
                    сплошной выборкой из памятников, находившиеся на особом хранении, оказались
                        влитыми в основную картотеку. Акта передачи-приема материалов не было составлено.</p>

                    <p>Позднее, в 1955 г., "по распоряжению директора ИЯ В.И.Борковского, его
                    заместителем К.А.Тимофеевым с помощью коменданта А.И.Воробьева без описи и
                    акта о передаче были отправлены в Москву Материалы ДРС, состоящие из: 1)
                    словоуказателей к отдельным памятникам, выборок из отдельных памятников,
                    материалов П.К.Симони, указателя источников Словаря И.И.Срезневского и некоторых
                    др[угих] материалов, хранившихся в так называемом "черном комоде". Все эти
                    материалы были вынуты, перевязаны и упакованы в ящик, в котором лежат до сих пор,
                        и следовательно не могут быть использованы;</p>

                    <p>2) рукописи I тома ДРС (в карточках);</p>

                    <p>3) материалов II и III томов ДРС, т.е. словарных статей, составленных в 1950-1952 гг.
                    С.Ф.Геккер, Л.Л.Кутиной, М.Д.Мальцевым, И.И.Матвеевым, Е.В.Матвеевой,
                        С.Л.Чернявской и И.Н.Шмелевой;</p>

                    <p>4) материалов Краткого ДРС, составленного до середины 1940 г., когда было принято
                        решение о создании многотомного словаря;</p>

                    <p>5) указателя источников ДРС (в карточках) и материалов, относящихся к нему;</p>

                    <p>6) разных архивных материалов (в карточках)",- так значится в Записке С.Ф.Геккер,
                        датированной 15 февраля 1957 г. (Архив КДРС. Папка N 17, с. 81).</p>

                    <p>С изъятием Картотеки ДРС обрывалась традиция, складывавшаяся в Ленинграде много
                    лет. Накопившие большой опыт работы над историческим словарем сотрудники не
                    могли продолжать свое дело. А лексикографы-историки, как известно по опыту,
                        формируются годами и десятилетиями.</p>

                    <p>"Но самым страшным, - говорится в записке "Исторический словарь,- было разрушение
                    того сложного аппарата, который представляли собой все материалы, на основе
                    которых [так - Л.А.] велась работа по составлению Словаря. Картотека является
                    основной, важнейшей частью, но все-таки частью необходимых для составления
                    Словаря материалов. Она была вырвана из этого сложного целого, тщательно
                    приспособленного для специфической работы... Однако важнейшая часть материалов -
                    подсобная библиотека текстов, создававшаяся в течение многих лет большим трудом
                    сотрудников, была разрушена, т.к. перевезти ее было нельзя: она состояла из книг
                    Библиотеки АН, Библиотеки Института, других библиотек... (Без такой библиотеки
                        невозможно работать).</p>

                    <p>Если бы картотека была оставлена в Ленинграде,- говорилось далее, - то временное
                    прекращение работы над Словарем древнерусского языка не принесло такого ущерба
                    ему, потому что работа над ним была бы уже давно возобновлена (ее уже можно было
                    возобновить в 1957-1958 годах), и мы сейчас имели бы не только первый, но второй и
                    третий, а возможно, и четвертый тома этого необходимого для всех русистов издания".
                    Так заканчивается Записка А.П.Евгеньевой "Исторический словарь" (Архив КДРС.
                        Папка N 25. С. 167).</p>

                    <p>В Москве Картотеку ДРС поручено было хранить сотрудникам Сектора истории
                    русского
                    языка и диалектологии ИЯ, которым руководил доктор филол. наук Р.И.Аванесов. О
                        продолжении словаря или о составлении нового в 1952 г. речи пока не было.</p>

                    <p>Однако потребность в словаре древнего русского языка оставалась. В ноябре 1954 г.
                    сотрудник Пушкинского Дома В.И.Малышев в газете "Советская культура" писал о
                    Картотеке ДРС: "Этот очень ценный словарный фонд должен быть положен в основу
                    работы по созданию словаря древнерусского языка". С интервалом в месяц (18
                    декабря) в той же газете напечатано мнение Д.С.Лихачева: "Необходимость в словаре
                    древнерусского языка давно ощущается в научных кругах, изучающих древнерусскую
                        культуру".</p>

                    <p>Эта тревога научной общественности за судьбу словаря и картотеки (см. еще статью
                    А.Морозова в газете "Литература и жизнь" от 28 июня 1959 г.) побудила директора
                    возобновленного в 1958 г. Института русского языка академика В.В.Виноградова дать
                    разъяснения о положении дел. В газете "Литература и жизнь" от 29 июля 1959 г. в
                    статье "Словари, которые очень нужны" он писал: "Сначала скажу о картотеке. Она
                    действительно в конце 1952 г. была перевезена в Москву. В 1953 г. она была
                    расставлена и сохраняется в полном порядке, о чем свидетельствует акт ее
                    обследования весьма авторитетной Комиссией в составе чл.-корр. АН СССР
                    Р.И.Аванесова и С.Г.Бархударова, профессоров А.П.Евгеньевой, С.И.Коткова,
                    Б.А.Ларина, В.Г.Орловой и канд. филол. наук Е.Н.Прокопович в начале 1957 г. ...С 1953
                    г.
                    в Картотеке работало более 120 человек [исследователей -Л.А.], многие из них
                    работали
                        более месяца.</p>

                    <p>Согласно решению Президиума АН СССР в 1952 г. работа по составлению Словаря
                    древнерусского языка была приостановлена в связи с срочной необходимостью
                    составления Словаря современного русского языка в 4-х томах (2 тома вышли из
                    печати, 3-й находится в производстве). Сотрудники древнерусского словаря были
                        переведены на составление академических словарей современного русского языка.</p>

                    <p>После организации Института русского языка в июле 1958 г. работа над древнерусским
                    словарем возобновилась. Имеющаяся картотека после серьезного ее пополнения
                    позволяет составить два словаря: древнерусский XI-XIV вв. (составление необходимой
                    для него картотеки будет в основном завершено в будущем году) и словарь XV-XVII вв.
                    под руководством акад. С.П.Обнорского. В этом году будет завершена работа по
                    написанию проспекта последнего словаря (инструкция для составителя и редакторов и
                        пробные словарные статьи).</p>

                    <p>Лексикографическая работа такова, что она дает свои результаты лишь постепенно,
                    по
                    прошествии многих лет". Как видим, работа по составлению исторических словарей
                    переносилась теперь в Москву, а по составлению словарей современного русского
                        литературного языка - оставалась в Ленинграде.</p>

                    <p>Эта статья акад. В.В.Виноградова требует некоторых пояснений и возвращения на два
                    с
                    половиной года назад. 10 дек. 1956 г. доктор филологических наук Р.И.Аванесов за
                    заседании ОЛЯ сделал доклад "О состоянии работы по подготовке древнерусского
                    словаря". В Постановлении ОЛЯ за подписью академика-секретаря В.В.Виноградова
                    говорилось: "Считать необходимым обследование картотеки Словаря древнерусского
                    языка", для чего была образована вышеупомянутая комиссия. К участию в ней
                    приглашены были член-корреспондент АН УССР Б.А.Ларин и доктор исторических наук
                    Л.В.Черепнин. А доклад Р.И.Аванесова считалось "целесообразным вынести на
                        широкое обсуждение очередного Общего собрания ОЛЯ" (Папка N 17, с.20).</p>

                    <p>17 января 1957 г. Р.И.Аванесов, в секторе которого хранилась Картотека ДРС,
                    выступил с докладом на Годичном собрании Отделения литературы и языка АН СССР "О
                    типах словарей древних восточнославянских языков и схеме построения словаря
                    древнерусского языка". До этого, в 1956 г., в ИЯ велась работа по составлению
                    предварительного проекта древнерусского словаря. Он обсуждался в академических
                    институтах Белоруссии и Украины, на Бюро ОЛЯ и на заседаниях Археографической
                    комиссии. Напомним о статье акад. Л.В.Щербы "Опыт общей теории лексикографии"
                        (Изв. ОЛЯ. 1940. N 3. С.89-117).</p>

                    <p>По мнению Р.А.Аванесова, "следует отграничить исторический словарь современного
                    языка от словарей той или иной древней эпохи из истории этого языка..." В первом
                    случае объяснения требуют факты современного языка, во втором - "факты древнего
                    периода этого языка в пределах, охватываемых словарем. Составление исторического
                    словаря русского языка - дело далекого будущего. В настоящее время актуальна
                    задача составления словарей отдельных периодов истории русского языка".
                    Р.И.Аванесов считает, что "целесообразно принять следующий план словарей: а)
                    словарь древнерусского языка (XI-XIV вв.), б) словарь старорусского языка (XV-XVII
                    вв.), в) словарь староукраинского языка (XV-XVIII вв.), г) словарь старобелорусского
                    языка (XV-XVIII вв.)... "Хронологические грани должны определяться не точной
                    датировкой, не формальным установлением рубежа, а кругом источников, их
                    характером". Эти словари должны быть лингвистическими, в которых будут
                    истолкованы слова, а не понятия и реалии. (Изв. ОЛЯ, 1957. Т. XVI. Вып. 3. С. 284-285).
                    Словарь древнерусского языка XI-XIV вв. должен был составляться на базе новой,
                    отдельной картотеки, о которой упоминает в своей статье академик В.В.Виноградов, а
                    для Словаря старорусского языка XV-XVII вв. пригодилась бы и Картотека ДРС, как
                    позднее решила Комиссия под председательством С.И.Коткова, обследовавшая ее в
                        начале 1957 г.</p>

                    <p>14 марта 1958 г. по Представлению Отделения литературы и языка Президиум АН СССР
                    принял постановление N 179 "Об организации Института русского языка на базе
                    русских секторов ИЯ АН СССР" во главе с академиком В.В.Виноградовым. Была
                    утверждена и новая структура Института языкознания. Словарный сектор оставался в
                    Ленинградском отделении ИЯ, а Группа древнерусского словаря вошла в Сектор
                    сравнительного и исторического изучения славянских языков (см.: ВЯ. 1958. N 4. С.
                        146).</p>

                    <p>В конце 1958 г. при Отделении литературы и языка была образована Словарная
                    комиссия под руководством Бюро ОЛЯ, призванная "согласовывать и объединять
                    творческие усилия научных работников институтов АН СССР, академий наук союзных
                    республик, кафедр высших учебных заведений и отдельных лиц в области разработки
                    вопросов лексикологии и лексикографии и в собирании словарных материалов".
                    Помимо других конкретных задач выдвигалось "издание непериодического органа
                    "Лексикографический сборник" (примерно два выпуска в год)" (Архив КДРС. Папка 17.
                        С. 158).</p>

                    <p>Это было время успехов фонетики и фонологии, время возникновения структурной
                    лингвистики, когда внедрялись методы точных наблюдений, шла речь об
                    автоматизации
                    лингвистических исследований. С другой стороны, были открыты первые берестяные
                    грамоты при раскопках в Новгороде, проводимых А.В.Арциховским. Казалось, что
                    только, словари, основанные на сплошном расписывании памятников отдельных
                    небольших периодов, могут представить историю русской лексики: ведь они имеют
                    возможность показать динамику различных языковых явлений того или иного периода,
                    аргументировать выводы статистическими данными, могут показать приуроченность
                    лексики к тем или иным жанрам письменности. Таким должен быть Словарь
                        древнерусского языка XI-XIV вв.</p>

                    <p>Книга под названием "Словарь древнерусского языка XI-XIV вв. Введение, инструкция,
                    список источников, пробные словарные статьи" под ред. Р.И.Аванесова вышла из
                    печати в 1966 г. В этом проекте высказывалась надежда, что словарь окажется
                    важным пособием для русистов, украинистов, белорусоведов и для славистов -
                    специалистов по южнославянским и западнославянским языкам, литературам и
                    истории, так как он основывается на базе "специально созданной картотеки, в которой
                    зафиксировано каждое слово - самостоятельное, служебное, частица, и притом не
                    только нарицательное, но и собственное имя (географическое и личное) - в каждом его
                    употреблении. Таким образом, картотека словаря исчерпывает весь языковой
                    материал в пределах принятых для него источников" (с. 5). Выписка материала
                    производилась по рукописям или фотокопиям, из изданных памятников брались
                    удовлетворительные в филологическом отношении. Так была создана Картотека
                    "Словаря древнерусского языка XI-XIV вв." (СДР), в которой представлено каждое
                    слово источника; каждая фиксация слова в тексте имеет свою цитату. Из 10 томов
                        этого Словаря к 1995 г. вышло 4 тома, охватившие словарный отрезок 1А-Моление 0.</p>

                    <p>Позднее доктор филол. наук В.Л.Виноградова сопоставила словники картотек СДР и
                    ДРС по периоду XI-XIV вв. и высказала мнение о том, что "картотеки самым
                    благоприятным образом дополняют друг друга, почти не повторяясь в общем
                    хронологическом периоде, что следует ожидать также и от словарей, составляемых на
                    их базе" (Проблемы славянской истори- ческой лексикологии и лексикографии... М.,
                        1975. В. 4. С. 78).</p>

                    <p>Что касается картотеки, привезенной из Ленинграда, тогда, в 1957 г., высказывались
                    сомнения в ее пригодности к какой-либо лексикографической обработке на том
                    основании, что она содержит 1выборки 0, т.е. памятники для нее расписывались
                    выборочно. Однако это не означало, что в ней представлена лексика в ограниченном
                    объеме. Исследование, проведенное Г.А.Богатовой в 1984 г., показало, что словник
                    картотеки ДРС намного обширнее, чем словник "Материалов" И.И.Срезневского и
                    словник картотеки СДР (см.: Богатова Г.А. История слова как объект русской
                        исторической лексикографии. М., 1984. С. 55-57, 72-77).</p>

                    <p>В "Памятке для выборщиков", помещенной в "Проекте" Б.А.Ларина 1936 г., читаем:
                    "При выборочной выписке материал из источников выписывается для большинства
                    слов. Нельзя выбирать только редкие или исчезнувшие слова... На одно слово из
                    каждого памятника может быть выписано несколько карточек в тех случаях, если
                    данный текст позволяет показать разные значения или оттенки значения выбираемого
                    слова, но на одно значение из каждого памятника достаточно и одной цитаты" (с.23).
                    Здесь выражалось доверие к выборщику, квалификация которого не подлежала
                    сомнению, к его опыту и знаниям. Недаром Б.А.Ларин так много внимания уделял
                    обучению молодежи в 1938 г. Вспомним его слова о том, что из студентов
                    Герценовского института он сумел подготовить "человек 20 прекрасных выборщиков".
                    В истории Картотеки ДРС бывали случаи отчисления выборщиков из-за
                    неудовлетворительности сданного материала, но об этом не хотелось бы здесь
                    упоминать: 1выборка 0 из памятников - не легкое дело, нужна большая начитанность в
                        древнерусской литературе, опыт работы с источниками.</p>

                    <p>Применялась в картотеке и 1полная 0 выписка "для текстов большой литературной
                    ценности, особенно известных и популярных в свое время", что означало "извлечение
                    из памятника 1всех 0 слов его лексического запаса в 1лучших, отобранных 0
                    контекстах. Разные значения и разные оттенки значений одного слова должны быть
                    представлены на отдельных карточках, так же, как отдельные слова" (Б.А.Ларин.
                    Проект, с.25). В "Проекте" Б.А.Ларин объяснял и сущность 1сплошной 0расписки
                    памятников: "Наиболее богатые по лексическому составу и типичные в литературном
                    отношении памятники... подвергаются сплошной выписке в некоторых частях или во
                    всем объеме , если памятник небольшой. Текст расписывается так, чтобы в словаре
                    было представлено 1каждое слово в 1каждом контексте 0 (там же). В таких случаях
                    "весь назначенный к сплошной выписке текст без всяких пропусков делится на
                    связные, законченные по смыслу части, в среднем по 5-6 строк или по 30-35 слов.
                    Важно выделять синтаксически связные целостные части" (с.26). Такая карточка
                    выписывалась 4 раза, а на верхнем поле ее в алфавитном порядке надписывались
                    начальные буквы всех входящих в цитату слов: весь текст такой карточки
                    предполагалось путем перестановки по алфавиту использовать последовательно для
                    всех этих слов (см. с.27). В Ленинграде эти материалы хранились отдельно от общего
                        фонда.</p>

                    <p>Защищая картотеку, Б.А.Ларин писал: "Достоинства Картотеки ДРС в том, что для
                    подавляющего большинства слов не только основного словарного состава языка XV-
                    XVII вв. (и первой четверти XVIII в.) она содержит богатую документацию, отборные и
                    многочисленные цитаты из многих тысяч памятников. Картотека отражает не только
                    лексику литературного языка (по письменным памятникам разных жанров), но и
                    народного разговорного и поэтического языка вследствие того, что широко
                    разработаны были архивы провинциальных съезжих изб, монастырей, церквей, частные
                    архивные собрания - в сфере деловой письменности, наиболее непосредственно и точно
                    отражающей народный обиходный язык" (Протокол обследования Картотеки ДРС в
                        1957 г. - Архив КДРС).</p>

                    <p>Им были вскрыты и слабые стороны этого собрания: недостаточность использования
                    памятников XI-XIV вв. (из 300 источников выписано всего 50 тыс. карточек). В
                    территориальном отношении слабо были представлены южновеликорусские и
                    центральные области. Эти недостатки, как теперь можно судить, были обусловлены
                    тем, что источники, созданные на юге, сохранились только с 1453 г., публиковались же
                    в основном новгородские и северновеликорусские тексты. Кроме того, длительное
                    время среди лингвистов было распространено мнение о том, что фонетический и
                    грамматический строй русского языка сформировался к XV в., и более поздние
                    источники, а тем более южновеликорусские, ничего нового прибавить не могут.
                    Напомним, что только полное обследование южновеликорусского наречия
                    (фонетическое, морфологическое, лексическое и синтаксическое), проведенное
                    С.И.Котковым (последнее - совместно с доктором филологических наук, профессором
                    Воронежского ун-та З.Д.Поповой) в 70-80 годах нашего столетия, несколько
                    поколебало
                        эту традиционную убежденность.</p>

                    <p>Чтобы решить вопрос о пригодности привезенной картотеки для словарных работ, и
                    была создана упомянутая В.В.Виноградовым Комиссия, работавшая в начале 1957 г.
                    (Архив КДРС. Протокол N 26 заседания Бюро ОЛЯ от 4 дек. 1956 г.). Она рассмотрела
                    материалы картотеки, подсчитала памятники, относящиеся к двум периодам развития
                    языка (XI-XIV вв. и XVI-XVIII вв.), проверила состояние карточек, ознакомилась с
                    указателем источников, с сопутствующими материалами: указателями слов к
                    отдельным памятникам, составленными еще в 30-40-х годах, с указателем источников к
                    Словарю И.И.Срезневского, с материалами картотеки по истории искусств П.К.Симони,
                    с материалами для однотомного Словаря древнерусского языка Д.И.Абрамовича, с
                    библиографической картотекой. Результаты ее работы были отражены в документе
                    "Итоги обследования Картотеки Словаря древнерусского языка" на 22 страницах.
                    Комиссия констатировала, что переезд в Москву неблагоприятно отразился на
                    состоянии картотеки: были отмечены утраты материала по нескольким словам,
                    неверная расстановка, которая также могла быть причиной утраты. Относительно
                    внешнего состояния карточек было записано так: "Стойкость чернил и бумаги...
                    такова,
                    что картотекой можно будет пользоваться еще не менее 100 лет (при благоприятных
                    условиях ее хранения), а карточки ак. А.И.Соболевского (уже теперь не совсем
                    благополучно сохранившиеся), в значительной мере дублированы и восполнены..."
                        (Архив КДРС. Папка N 25. Л. 6. Далее - Итоги).</p>

                    <p>Отмечалась уникальность этого картотечного собрания: "Характеризуя в целом
                    Картотеку ДРС, следует прежде всего указать на то, что в ее составлении принимали
                    участие выдающиеся лингвисты, литературоведы, историки и археологи, причем это
                        было участие в самой предварительной, "черной" работе по расписыванию памятников.</p>

                    <p>К расписыванию памятников руководством ДРС были привлечены
                    высококвалифицированные лица, хорошо начитанные в древнерусских литературных и
                    деловых памятниках, знающих реалии соответствующих эпох, историческую
                    обстановку,
                    литературную историю расписываемых памятников" (Итоги, с.17). Отмечались и
                    положительные черты картотеки: "Особенно удачным представляется выбор
                    памятников с точки зрения охвата специальной терминологии. В картотеке
                    представлены памятники и документы, относящиеся к сельскому хозяйству, торговле,
                    мореходству, математике, скотоводству, коневодству, псовой, ястребиной и соколиной
                    охоте, артиллерии, пиротехнике, рудознатству, иконографии, книгописному и
                    переплетному делу, медицине, риторике, богословию и пр. ...Выписки сделаны из
                    различных описей имуществ (интересны, например, описи одежд), переписных и
                    окладных книг, делопроизводственных документов, законодательных памятников,
                        календарей, частных писем, азбуковников, словотолкователей, букварей и пр." (с.18).</p>

                    <p>Отмечалось богатство фоновых материалов, в частности фольклорных, наличие
                    диалектных данных. Положительным было то, что она содержала значительные
                    выписки
                    из рукописей. Достоинством картотеки считалось "наличие в ней двуязычных выписок
                    из переводных памятников", параллельных словарных данных из переводов с
                    греческого, латинского, польского и некоторых других языков (с.20). Высказывалось
                    пожелание, какие памятники следовало бы расписать, чтоб заполнить лакуны. Так, из
                    Ипатьевской летописи было сделано всего З92 выписки, в то время как из
                    Новгородской первой - 4000, из Львовской - около 6000, из Никоновской - около 7500
                        выписок. Уваровскую летопись еще не успели расписать - она была издана в 1949 г.</p>

