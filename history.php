                <h2 id="part1">Часть 1</h2>
                <div>
                    <p>В Москве в Институте русского языка им. В.В.Виноградова Российской Академии наук (ул. Волхонка, 18/2) хранится уникальное рукописное картотечное собрание, известное как Картотека ДРС (древнерусского словаря). Ее составляют карточки с выписками из рукописных и опубликованных текстов XI-XVIII вв. Количество их приближается к 2 миллионам. Карточки содержат выписки из летописей, из повестей и посланий, из житий и хождений, из деловых книг и грамот, из частных писем и государственных постановлений. Материалы Картотеки ДРС охватывают почти всю русскую письменность. В ней есть и отрывки из рукописей, утраченных или погибших во время Великой Отечественной войны 1941-1945 гг. Здесь представлены произведения, переведенные на русский язык в разное время, и самобытные, возникшие на Руси. Все богатство русской письменной культуры, начиная с XI в. вобрала в себя Картотека ДРС.</p>
                    <p>В настоящее время фонды Картотеки ДРС являются объектом внимания русистов и славистов всего мира. За последние 2О лет исследователи из 16 зарубежных стран и 89 городов России и стран СНГ обращались в КДРС. С привлечением ее материалов разрабатывается около 500 тем не только лингвистического, но и исторического, этнографического, социально-экономического, юридического характера, исследуется история складывания науки и национальных истоков научной терминологии математики, механики, физики, химии, медицины, горного дела и других естественно- научных и гуманитарных направлений. Картотека ДРС становится необходимой составной частью различного рода историко-культурных исследований. Обилие описания исторических реалий делает ее надежным источником для "Русской энциклопедии", работа над которой только начинается.</p>
                    <p>Главное же ее детище - издающийся с 1975 г. "Словарь русского языка XI-XVII вв." (далее - СлРЯ). По охвату времени, по возможностям словарной статьи этот словарь - флагман среди словарей исторического жанра (в 1995 г. вышел 21-й выпуск). Он наиболее полно освещает словарный состав русского языка древне- и старорусского периода, на основе того потенциала, который был заложен основателями Картотеки ДРС - академиками А.И.Соболевским и М.Н.Сперанским. Они предполагали создать картотеку контекстов, которые были бы ориентированы на исчерпывающее и наиболее яркое раскрытие значения слова, на полноту его словообразовательного гнезда. Ставилась цель дать слово в сопоставлении, т.е. в ряду синонимов и антонимов, предусмотреть выписки, содержащие историко-культурные сведения в описании реалий и понятий.</p>
                    <p>Эта картотека была собрана учеными, чьим знаниям и опыту можно доверять, особенно когда речь идет об отборе лексики для древнерусского словаря, о подборе памятников разного времени, которые бы отражали специфику русской письменной истории. В 1957 г., когда встал вопрос о судьбе Картотеки ДРС, Д.С.Лихачев писал: "Следует прежде всего указать на то, что она в основном составлена выдающимися лингвистами, литературоведами и историками, принявшими участие в самой предварительной, "черной" работе по расписыванию памятников... Можно с уверенностью сказать, что такого состава крупнейших ученых, которые бы в такой степени были начитанны в древнерусских текстах, собрать для написания карточек ДРС больше не удастся... Картотеку ДРС, составленную учеными, всесторонне изучившими первые восемь веков русской истории... следует уже по одному этому признать уникальной" (Д.С.Лихачев. Заключение об "Указателе источников Картотеки ДРС". Рукопись. - Цитируем по статье: "50 лет Картотеке ДРС" // Проблемы славянской исторической лексикологии и лексикографии. Тезисы конференции. Октябрь 1975 г. Москва. Вып.4. Теория и практика исторической лексикографии. М. 1975.- С.37-38).</p>
                    <p>Эти обстоятельства делают органичным использование ее материалов и при подготовке других выходящих в последние 30 лет словарей. Таковы "Этимологический словарь славянских языков" под ред. академика О.Н.Трубачева (т.1-21. 1974-1995), "Этимологический словарь русского языка" под ред. Н.М.Шанского (т.1-8. 1963- 1982), "Словарь-справочник "Слова о полку Игореве" В.Л.Виноградовой (т.1-6. 1965-1982).</p>
                    <p>Внимание русистов и славистов, их тревога связаны отчасти с тем, что в СлРЯ XI-XVII вв. и другие словари из картотеки попадает лишь около четверти документирующего (цитатного) материала. Увидит ли ученый мир (успеет ли?) накопленные картотекой богатства в полном объеме? Многие карточки содержат небезынтересные для науки языковые параллели, комментарии к тексту и замечания тех ученых, которые делали выборки.</p>
                    <p>XI Международный съезд славистов в 1993 г. в Братиславе под председательством
                    проф. Я.Дорули обратился в ЮНЕСКО в связи с объявленной программой "Память
                    мира", посвященной архивации ценнейших национальных сокровищ, находящихся под
                    угрозой их утраты, с предложениями, касающимися Картотеки ДРС. В 70-х годах
                    американские библиотекари забили тревогу: бумага ХХ века, изготовленная из
                    древесной массы с содержанием квасцов, имеет большие кислотные остатки, которые
                    приводят к тому, что через 75-100 лет бумага делается хрупкой и постепенно
                    саморазрушается. Лаборатории ищут пути замедления процесса "дряхления бумаги":
                    обрабатывают книги лист за листом в щелочном растворе. А картотеки? Один из путей
                    Д.Дж.Бурстин (Библиотека Конгресса, США) видит в разностороннем применении
                        компьютеров и создании телекоммуникационных сетей.</p>
                    <p>Предложение Российской стороны на съезде о развитии новых технологий для
                    перевода картотек исторических словарей на безбумажные носители (в частности,
                    Картотеки ДРС - на твердые оптические диски CD-ROM) встретило одобрение и
                    поддержку со стороны Международной комиссии по лексикологии и лексикографии
                    (председатель - акад. Саксонской Академии наук Х.Шустер-Шевц), со стороны
                    Международной Библейской комиссии (председатель - доктор филологических наук
                    РАН, проф. А.А.Алексеев), со стороны Международной Академии информатизации (акад.
                    Л.В.Златоустова) и многих других лингвистов Европы, Азии и Америки. Высказывалось
                    пожелание о создании единого банка данных славянских исторических картотек. Пока
                        же предложено было начать с Картотеки ДРС (КДРС).</p>

                    <p>В 1995 г. Картотеке ДРС официально исполнилось 70 лет, однако многие ее карточки
                    гораздо "старше". Она вся написана на бумаге ХХ века разной плотности и формата:
                    иногда это старые плакаты, конверты, пригласительные билеты и извещения, листовки,
                    поля книг и газет и др. Ее материальное состояние отражает историю России начала
                    века: войны, революции, эвакуации, хранение в блокадном Ленинграде, переезд в
                    Москву, хранение в пачках и раскладка в ящики, пожары в соседних помещениях и
                        наводнения - так много всего выпало на долю этих хрупких листочков.</p>

                    <p>По необыкновенному стечению обстоятельств Картотека ДРС в Москве хранится в
                    здании, находящемся рядом с тем местом, где был взорван Храм Христа Спасителя.
                    Поистине свято место пусто не бывает! Картотека после переезда из Ленинграда сама
                    была храмом нашей духовной жизни. Ее сокровища находились не в сейфах, она
                    постоянно была в работе у отечественных и зарубежных специалистов-исследователей -
                    словарников, филологов, историков, писателей, журналистов, от академика до
                    аспиранта. Это ускоряло процесс старения бумаги. Кроме того, неблагоприятно
                    воздействует на ее состояние экология крупного города, и в частности того района, где
                        расположен Институт русского языка РАН.</p>

                    <p>Здание Института русского языка, построенное в XVIII в., - единственное в этом районе,
                    сохранившееся после пожара 1812 года. Мелкое залегание линии метро, работы по
                    восстановлению Храма Христа Спасителя в непосредственной близости от него,
                    начатые в 1994 г., реконструкция близлежащих зданий еще долгие годы будут держать
                        Картотеку в зоне риска.</p>

                    <p>В других странах с картотеками подобного ранга обращаются иначе. С ними тоже
                    работают, как например, во Франции, в Нанси, а страховой дубль (9 миллионов
                        карточек) хранят в Национальном банке в Париже.</p>

                    <p>* * *</p>

                    <p>История Картотеки ДРС не будет понятна без истории Академии наук в России,
                    деятельности ее отделений, комиссий, той преемственности научных идей и научных
                        предприятий, которая существовала между XVIII, XIX и XX веками.</p>

                    <p>Известно, что Академия наук возникла в Санкт-Петербурге во времена Петра I: указ
                    был подписан 28 янв. 1724 г. Широкое развитие в ней получили точные и естественные
                        науки. Официальным языком науки была латынь.</p>

                    <p>Энциклопедизм ученых XVIII в., и в частности М.В. Ломоносова, ученого-
                    естествоиспытателя, сделавшего свой вклад в объяснение строения вселенной,
                    химических соединений, атмосферного электричества, автора практических руководств
                    по металлургии и освоению Северного морского пути, проявился в том, что он был
                    вместе с тем выдающимся поэтом, филологом и историком, автором "Российской
                    грамматики" (1755) и "Древнерусской истории" (1765). Его ученики не только первыми
                    стали публиковать русские рукописи, они накапливали материал для русских словарей
                        и участвовали в их создании.</p>

                    <p>Во времена Екатерины II, в 1783 г., была основана Российская Академия наук во главе
                    с президентом Е.Р.Дашковой, целью которой было "усовершить и возвеличить русский
                    язык" как язык науки, образования, культуры. Тогда-то и началась работа над первым
                    "Словарем Академии Российской", в которой приняли участие ученики М.В.Ломоносова
                    естествоиспытатель И.И.Лепехин, математик С.Я.Румовский, поэт Г.Р.Державин,
                    драматург Д.И.Фонвизин, историк и публицист М.М.Щербатов, ученики Петербургской
                    академической гимназии, а затем члены Академии Российской М. и А.Соколовы.
                    Словарь был создан в необычайно короткие сроки: в 1789 г. вышел первый том, а в
                        1794 г. его издание в 6 томах было завершено.</p>

                    <p>В XVIII в. и русская история начала обретать силу в опоре на документальные
                    свидетельства и разыскания в архивах. В опубликованной в 17?? г. "Истории
                    Российской" М.М.Щербатов использовал 21 список русских летописей. В своей
                    "Истории", доведенной до 1019 г., М.В.Ломоносов постоянно цитировал "летопись
                    Нестора". "История Российская с древнейших времен" В.Н.Татищева при его жизни не
                    была опубликована (в 1786 г. ее издал Г.Ф.Миллер). Она была построена на материале
                    многих рукописных источников, которые имелись в распоряжении автора. Более того,
                    он дважды посылал в Академию наук подготовленное им к изданию "Собрание законов
                    древних русских", в которое входила "Русская Правда" и "Судебник царя Ивана
                    Васильевича" 1550 г. А во вторую редакцию 1750 г. В.Н.Татищев включил и "Указы
                        дополнительные к Судебнику". Их также издал Г.Ф.Миллер в 17?? г.</p>

                    <p>Самой ранней серьезной публикацией русских рукописей можно считать помещенные в
                    подстрочных примечаниях к "Описанию Сибирского царства" (1750) Г.Ф.Миллера
                    грамоты, переписанные для автора в сибирских архивах во время его путешествия по
                    Сибири (1733-1742). Русские летописи впервые были опубликованы в 1767 г. Это была
                    Радзивиловская (Кёнигсбергская) летопись, подготовленная к печати И.С.Барковым, и
                    Никоновская, подготовленная А.Л.Шлецером и С.Башиловым. 1767 год можно считать
                    годом начала издания рукописей в России: в этом же году А.Л.Шлецером была издана и
                    "Русская Правда" по списку Новгородской летописи, по которому готовил к изданию
                    этот памятник и В.Н.Татищев. К концу XVIII в. почти все списки русских летописей были
                    опубликованы. Вышли и два издания "Древней Российской Вивлиофики" Н.И.Новикова
                        (1773-1775, 1788-1791), которые показали неисчерпаемое богатство русских архивов.</p>

                    <p>В начале XIX в. Петербургская и Российская академии инициировали деятельность
                    таких археографов, как профессор Московского университета Р.Ф.Тимковский,
                    подготовивший к изданию 13 листов Лаврентьевской летописи по списку, сгоревшему в
                    1812 г., К.Ф.Калайдович и П.М.Строев, впоследствии ставшими членами Петербургской
                    академии. К.Ф.Калайдович в 1824 г. издал подготовленную Р.Ф.Тимковским летопись и
                    еще множество памятников в двух выпусках "Русских достопамятностей" (1815, 1843).
                    Ему принадлежит и издание произведений писателя XII в. Кирилла Туровского а
                    "Памятниках Российской словесности" (1821), исследование "Иоанн, екзарх
                    Болгарский" (1824), которое также содержало древние тексты. Болезнь и смерть
                    К.Ф.Калайдовича в возрасте 40 лет не позволили осуществиться его обширным планам
                    по изданию русских летописей. Одной из самых значительных его публикаций остается
                    второй том "Собрания государственных грамот и договоров" (1819), в котором он
                    передавал рукописи, используя все древнерусские буквы и даже надстрочные знаки. Он
                    был первым, кто сформулировал правила издания древних русских рукописей, правда,
                        они так и остались в его Доношении к А.Ф.Малиновскому от 21 января 1821 г.</p>

                    <p>В 1828-1834 гг. П.М.Строев организовал знаменитую Археографическую экспедицию,
                    которая за 6 лет побывала в 14 губерниях и обследовала около 200 библиотек и
                    архивов. Было собрано до 3000 актов и составлен каталог рукописей и старопечатных
                    книг. Для обработки и издания этих материалов была создана вне Академии
                    специальная Археографическая комиссия. Часть обнаруженного рукописного
                    материала описана П.М.Строевым в книге "Библиологический словарь" (1882 г.), часть
                    скопирована и приобретена в собственность Публичной библиотекой в Петербурге.</p>

                    <p>Существующая с 1834 г. Археографическая комиссия занималась публикацией
                    рукописей как исторических источников. Среди ее серийных трудов - "Полное собрание
                    русских летописей" (начиная с 1846 г.), "Русская историческая библиотека" (т.1-39. 1875
                    -1927 гг.), "Акты исторические" (т.1-5. 1841-1842 гг.) и "Дополнения" к ним (т.1-12. 1846-
                    1872 гг.). Во главе этой комиссии в конце XIX- начале XX в. стоял академик Л.Н.Майков,
                        а в 1918-1929 гг. - профессор истории С.Ф.Платонов.</p>

                    <p>* * *</p>

                    <p>По принятому в 1836 г. Уставу к Петербургской Академии наук была присоединена
                    Российская Академия в форме Отделения русского языка и словесности: "10 октября
                    1841 г. по предложению президента Академии наук С.С.Уварова было установлено
                    новое организационное устройство Академии наук. Все изучаемые Академией науки
                    делились теперь на три Отделения: 1) физико-математических наук (ОФМН); 2) русского
                    языка и словесности (ОРЯС)... и 3) исторических наук и филологии (ОИФ)" -
                    (Г.Д.Комков, Б.В.Левшин, Л.К.Семенов. Академия наук СССР. Краткий исторический
                        очерк. М., 1974. - С. 157. Далее -- Комков).</p>

                    <p>Именно в Отделение русского языка и словесности в мае 1925 г. обратился академик
                    А.И.Соболевский с предложением о создании картотеки. В Докладной записке он
                    обосновал задачу собирания материалов по памятникам письменности для ряда
                    словарей языка древнерусского периода. Он предполагал продолжить начинание
                    академика И.И.Срезневского, "Материалы для словаря древнерусского языка по
                    памятникам письменности" которого издавались в России с 1890 по 1912 г. под
                    наблюдением академика А.Ф.Бычкова, а после его кончины, с 1899 г. - академика
                        А.А.Шахматова.</p>

                    <p>Выход в свет этого словаря был одним из крупных актов Отделения русского языка и
                    словесности и в какой-то мере фамильным обязательством Срезневских, которое они с
                    честью выполнили. Около 40 лет (с 1858 г.) собирал И.И.Срезневский картотеку для
                    словаря по письменным памятникам XI-XIV вв., держал корректуру пробных его
                    образцов. После его смерти картотеку приводили в порядок, пополняли и издавали по
                    правилам, выработанным при жизни отца, его дети Ольга и Всеволод. В томе
                    дополнений они расширили круг памятников за счет источников XV-XVI вв. (около 470) и
                    единично - XVII в. Выпуск словаря был закончен к столетию со дня рождения отца, к
                    1912 году, и О.И. и Вс.И. Срезневские были избраны членами-корреспондентами
                    Академии наук. "Таким образом, русская историческая лексикография реально начала
                    складываться как самостоятельная область... в конце XIX - начале XX в." (Г.А.Богатова.
                        История слова как объект русской исторической лексикографии. М., 1984. С.47.).</p>

                    <p>В лице И.И.Срезневского, В.И.Даля Россия XIX века имела лексикографов мирового
                    класса, таких, как Я.Гримм или Э.Литтре. В составе ОРЯС, которым долгое время
                    руководил И.И.Срезневский, всегда было немало блестящих филологов, историков,
                    этнографов: Ф.И.Буслаев, И.В.Ягич, Я.К.Грот, М.И.Сухомлинов, А.Н.Веселовский,
                    А.А.Шахматов, Н.К.Никольский и др. Само Отделение занимало почетное место в
                    структуре Академии. По положению, в круг его занятий входило исследование свойств
                    русского языка, создание словарей, отражающих его современное функционирование и
                    его историю; славяно-русская филология вообще, изучение славянских языков и
                    диалектов в их отношении к русскому языку, составление сравнительных, общих,
                    словопроизводных и др. словарей, издание рукописных памятников. Отделение
                    располагало большей самостоятельностью, чем другие: оно имело
                    председательствующего, "который назначался президентом Академии из среды
                    академиков этого Отделения и утверждался министром народного просвещения...
                    Второе Отделение... жило по своим, отличным от других Отделений, традициям и
                    установкам" (Комков, с.157). Его члены, в отличие от членов I и III отделений, не
                    получали содержания: они "за исключением <<особ духовного звания>>, считались
                        находящимися на государственной службе, но им не выплачивалось ни жалованье, ни
                            пенсия" (Там же, с.157).</особ>

                        <p>Академик В.М.Истрин, исполнявший обязанности председательствующего в 1923 г., и
                        академик М.Н.Сперанский в Докладной записке "В защиту ОРЯС" писали: "Отделение
                        ясно осознавало свою высокую национальную задачу и быстро оценило всю важность и
                        все значение своего существования, как отдельного целого, ставшего во главе целой
                        научной области" (СПб Отделение Архива РАН, ф.172, ед.хр.13, л.66-67).</p>

                        <p>Кроме трехтомного издания "Материалов" И.И.Срезневского, с 1891 г. шло издание
                        другого гиганта - "Словаря русского языка" Грота - Шахматова (т.1: А-Д. 1891-1895, ред.
                        Я.К.Грот; т.2: Е-З. 1895-1916, ред. А.А.Шахматов). "Если в настоящее время и можно с
                        успехом работать над русским языком во всем его разнообразии, над историей
                        литературы до-Петровского периода и над славяноведением в широком смысле, -
                        читаем далее в той же Записке, - то всем этим русская наука обязана главнейшим
                        образом Отделению русского языка и словесности... Достаточно указать на 100 томов
                        "Сборника" и на 35 томов старых и новых "Известий", заключающих в себе массу
                        материала и по языку, и по истории, и по литературе, и по этнографии... Издания
                        Отделения ... немало способствовали возвышению престижа Академии и уважения к
                        русской науке вообще... Кроме чисто научной стороны есть другая, где Отделение уже
                        выступает из замкнутых стен Академии и получает значение научно-общественное.
                        Наука движется не одними академиками, а совокупностью сотен и тысяч лиц, стоящих
                        вне Академии, но объединенных какой-либо силой, какой-либо идеей, внутренней и
                        внешней" (Там же, л.67). Уже во времена И.И.Срезневского была традиция:
                        публиковались и рассылались анкеты с различными вопросами - и в адрес ОРЯС
                        приходили материалы с записью народной речи, с толкованием тех или иных слов и
                        выражений и от ученых других специальностей, и от учителей, и от духовных лиц. Многие
                        из этих данных были использованы в "Дополнениях к Опыту областного великорусского
                        словаря" (1858), в публикациях ОРЯС, в "Толковом словаре живого великорусского
                            языка" В.И.Даля (1861).</p>

                        <p>В 1899 г. академиками М.И.Сухомлиновым и А.А.Шахматовым был составлен "Проект
                        издания собрания сочинений русских писателей" и образована при ОРЯС Комиссия по
                        изданию памятников древнерусской литературы (КПДЛ), которой до 1917 г. руководил
                        академик В.М.Истрин, а потом, до 1936 г. - академик Н.К.Никольский. Она должна была
                        опубликовать все памятники по лучшим спискам. В 1912 г. С.П.Розановым было издано
                        "Житие преподобного Авраамия Смоленского и службы ему", в 1916 г. Д.И.Абрамович
                        опубликовал "Жития святых мучеников Бориса и Глеба и службы им". В 1932 г.
                        Н.Н.Зарубин подготовил к печати и издал III выпуск из серии "Памятники древнерусской
                        литературы" - "Слово Даниила Заточника по редакциям XII и XIII вв. и их переделкам". В
                        1934 г. вышел четвертый (и последний) выпуск этой серии "Из старинной новгородской
                            литературы XIV века" М.Н.Сперанского.</p>

                        <p>Помимо работ КПДЛ, рукописи готовились к изданию и другими учеными. Академик
                        А.И.Соболевский, задумывая собирать картотеку древнерусского словаря, также
                        обратился с письмами к ряду ученых в разных городах России, работавших над теми или
                        иными памятниками письменности, особенно по периоду ХV-ХVII вв., не охваченному
                            "Материалами" Срезневского.</p>

                        <p>К 20-м годам ХХ в. в содружестве с ОРЯС работало двенадцать комиссий, развивавших
                        различные области русского и славянского языкознания. Некоторые из них
                        насчитывали до 50 членов. "Достаточно указать на такие комиссии, как Комиссия по
                        составлению Словаря русского языка, Пушкинская комиссия, Диалектологическая
                        комиссия, Комиссия по изданию памятников древней письменности, Комиссия по
                        изданию Библиотеки русских писателей, Комиссия по составлению Толковой
                        библиографии, Комиссия по научному изданию Славянской Библии, не говоря уж о
                            других более мелких", - писал Истрин (Там же, л.70).</p>

                        <p>Большая часть комиссий была связана с описанием сокровищ рукописных отделов,
                        изданием памятников письменности. Именно отсутствие публикаций значительных
                        произведений русской старины было одной из причин того низкого, "прискорбного
                        состояния научных знаний о судьбе ...древнерусской литературы, в каком они
                        находились в 80-90-х гг. прошлого столетия и продолжают оставаться доныне", и
                        которое, как писал Н.К.Никольский в 1929 г., "удручает" (Задачи и краткий очерк
                        деятельности Комиссии по изданию памятников древнерусской литературы со времени
                            ее возникновения до 1 января 1929 г. Л. 1929. - С. 2).</p>

                        <p>Поэтому перед КПДЛ Отделение русского языка и словесности поставило две главные
                        задачи. Первая: предварительно определить объем материала, составить полный
                        список не только тех литературных произведений, которые подлежат обнародованию, но
                        и тех, которые должны быть приняты во внимание при издании, хотя и находятся в
                        разных хранилищах. И вторая задача: одновременно с этой работой решено было
                        приступить к снятию копий с древнейших и лучших списков. Проф. Н.К.Никольский со
                        своим помощником Ф.И.Покровским посетили 35 хранилищ рукописных собраний в
                        разных городах в 1899-1900 гг. Москва, Тверь, Ярославль, Владимир, Кострома,
                        Казань, Вологда, Архангельск, Соловки, Вильно, Киев, Тула и другие города открывали
                        перед ними сокровищницы древних рукописей. Позже они побывали в Новгороде,
                        Пскове, Полтаве, Кишиневе, Львове, Петрозаводске. По их наблюдениям оказалось,
                        что некоторые произведения существовали в единственном списке, в то время как
                            другие насчитывали по нескольку тысяч.</p>

                        <p>В это же время М.Н.Сперанский, преподаватель Нежинского Историко-
                        филологического института (до 1906 г), также в поисках славянских и русских рукописей
                            обследовал архивы Праги, Загреба, Белграда, Софии, Вены и др. европейских городов.</p>

                        <p>"Ученая деятельность Отделения с течением времени вышла из круга чисто русских
                        интересов и вступила на широкую дорогу изучения всего славянства вообще",- как
                        писал Истрин,- на путь поддержания русско-славянских связей. Содружество со
                        славянскими землями проявлялось прежде всего в связях с Академиями - Чешской,
                        Сербской, Юго-славянской, Болгарской. "Славянские Академии по своим силам не
                        могут равняться с большими академиями - как Берлинская, Венская, Парижская, с
                        которыми Российская Академия в целом, как равноправный член, состоит в союзе.
                        Славянские академии имеют более узкую цель - изучение, прежде всего, своей страны,
                        своего всего родного, т.е. именно ту цель, которую несет и Отделение русского языка и
                            словесности" (В защиту ОРЯС, л.71-72).</p>

                        <p>Сближение и развивающиеся связи славистов привели к мысли о создании Союза
                        славянских академий и ученых обществ (1912 г.) для выполнения крупных задач,
                        например, по подготовке и изданию "Энциклопедии славянской филологии", по
                        публикации памятников письменности, накоплению материалов для словарей
                        исторического жанра. Определяя роль ОРЯС, Истрин писал: "В данном случае важен тот
                        престиж, который Отделение приобрело среди ученого славянского мира и который
                            предоставил ему главную руководящую роль в союзе" (там же, л.72).</p>

                        <p>Так исподволь подготавливалась база для создания новых картотек и словарей, шло
                        накопление материалов и наблюдений для издания библиографии писателей древности.
                        Многие ученые работали в составе сразу нескольких комиссий и способствовали своей
                        деятельностью уже на тех ранних этапах осуществлению идеи формирования Комиссии
                            по собиранию материалов для древнерусского словаря.</p>
                </div>
