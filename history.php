                    Императорская
                    Академия наук была переименована (в мае 1917 г.) в Российскую Академию наук.
                    Последовавшая затем революция в октябре 1917 г. привела к эмиграции части ученых.
                    Гражданская война и явления, связывавшиеся тогда с "обострением классовой
                    борьбы",
                    коснулись, к сожалению, и Академии наук, в чем можно убедиться сегодня при
                    знакомстве
                    с материалами архивов, книг серии "Репрессированная наука", с материалами
                    вышедшего
                    в 1993 г. 1-го тома книги "Академическое дело" (далее - АД.1), книги Ф.Д.Ашнина и
                    В.М.Алпатова "Дело славистов. 30-е годы" (1994), с материалами вышедшего в 1993 г.
                    1-го
                    тома книги "Академическое дело" (далее - АД.1). Как говорится во вступительной
                    статье
                    этой книги, "с Академией наук у Правительства были давние счеты. Академия
                    отнеслась к
                    Октябрьской революции враждебно. Общее экстраординарное собрание Академии 21
                    ноября (3 декабря) 1917 г. единогласно приняло резолюцию против "насильников,
                    захвативших власть" (АД.1. С.XII) и призвало присоединиться к ней высшие учебные
